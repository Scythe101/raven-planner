<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">Audition Only (Chorus prior highly recommended)</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed to provide experienced singers with challenging repertoire spanning the
	periods in classical music history, as well as non-western folk traditions. Students will study
	and perform in different languages, and learn performance practice specific to the genres. Special
	focus will be given to the rudiments of healthy vocal technique, advanced sight reading, aural
	skills, and part independence. The ensemble will rehearse daily for on-site and community
	performances.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Repertoire covered in the course is intended for students who are proficient at sight reading, and
	have had previous choral ensemble experience. Students are encouraged to participate in honor
	choir, and pursue extracurricular ensembles and solo performance opportunities. Eligibility is
	based on audition; it is highly recommended for students to take Chorus prior to this class.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This is a year-long class; it is not advised by the instructor to take this class for only one
	term.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	<strong>Performances:</strong> Students will perform on campus and in the community as a part of this
	class. These performances are generally on evenings and weekends. Students will be notified well in
	advance of these dates.
</p>

<h2 class="mt-8">Grading</h2>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>60% Classwork</li>
	<li>30% Performance Assessments</li>
	<li>10% Aural Skills / Written Work / Music Theory</li>
</ul>
