<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	<strong>Course Overview:</strong><br>
	The mission statement for the Peer Assistance Leaders (PALs) is “PALs promote student wellness and encourage a safe and positive school community.” In the pursuit of this goal, the course offers an introduction to peer counseling, project design and management, public speaking and leadership, financial management, fundraising, and publicity.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	PALs are expected to learn the fundamentals of active listening used by professional counselors including, perception checking, clarifying, acknowledgement, summarizing, and mirroring. Each student in PALs receives a six hour training from credentialed members of the counseling staff and PALs advisor. Prior to actually counseling another student, the PALs will compile background on the student, formulate a possible strategy to help, and plan follow-up meetings.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	PALs are expected to create projects with a focus on improving school wellness. During the course of the year, each student will conceive an idea, present it to the class for approval, select a team, implement the project, and conduct a debrief of the event. All projects incorporate detailed record keeping of contacts, financial costs, project outlines, and any difficulties in the planning or implementation of the project. All projects require an estimated budget and fundraising to secure monies.  All projects require coordinating with staff members to secure facilities, get adult volunteers when necessary, and publicize the event. All projects go through a post-mortem review with the PALs advisor and PALs managers that serves as the final assessment of the project.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Grading is based on performance across various categories including weekly check-in sheets, PALs hours, and outreach activities. Be advised that this can include work to support activities outside of the school day (25 hours per semester). You must meet the requirements and do the assigned/expected work if you want to earn a high grade.
</p>

