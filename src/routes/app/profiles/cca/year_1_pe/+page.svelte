<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Year 1 PE is an integrated course which allows students to fulfill both their PE and Health course requirements for graduation. The majority of class time will be devoted to Physical Education (PE). Students will complete the majority of the "Health Education" portion of the class online. Students will take Health Education exams on campus with their PE teacher at designated times over the course of the term.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Physical Education portion of the class will introduce students to a variety of individual and team sports, teach the benefits of exercise through both cardiovascular and strength training, and introduce several fitness concepts in hope of providing each student with the desire to continue a lifestyle of physical activity throughout and beyond high school.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Health portion of the class will consist of the following key health education units:
</p>
<ol class="list-decimal ml-8 mr-4 mb-4 text-xl 2xl:w-3/4">
	<li>A Healthy Lifestyle</li>
	<li>Skills and Character</li>
	<li>Stress and Loss; Achieving Emotional Health</li>
	<li>Eating Behaviors, Weight, and Body Composition</li>
	<li>Tobacco, Alcohol, and Drugs</li>
	<li>Healthy Relationships</li>
	<li>Healthy Peer Relationships</li>
	<li>Noncommunicable Diseases and Disabilities</li>
	<li>Communicable Diseases and Sexually Transmitted Infections</li>
	<li>Reproduction, Development, and Birth</li>
	<li>LGBTQ</li>
</ol>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Please note: A student may opt out of the Growth, Development and Sexuality unit of this course.
</p>

