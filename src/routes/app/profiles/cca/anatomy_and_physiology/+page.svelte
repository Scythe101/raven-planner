<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The purpose of this course is to expose students to the biology of the form and function of the
	human body and its similarities/differences with other species. It is a rigorous second year
	biology course for students interested in biology, medicine and its related professions.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students will be introduced to many lab exercises that will help them to understand both the human
	body and the importance of correct laboratory procedures, including performing multiple
	dissections of individual organs and whole animal specimens.
</p>

