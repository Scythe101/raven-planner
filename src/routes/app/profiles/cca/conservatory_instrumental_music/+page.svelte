<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Instrumental Music Conservatory is an audition-based, after-school extension of the daytime Envision program, offering smaller class sizes and more individualized instruction for the most advanced music students.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students involved with the Instrumental Music Conservatory will focus on individual musicianship and stretching their conception of musical artistry. The primary focus of the Instrumental Music Conservatory is to create comprehensive and capable musicians, by focusing on individual technique and solo and small ensemble performance. In addition to recitals, concerts, and theatrical productions, students will participate in numerous workshops taught by our staff of teaching artists. Students will study composition, arranging, music technology and recording arts as well.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The collaborative nature of the arts ensures that Conservatory students enjoy many opportunities to work with and learn from one another. A focus on interdisciplinary collaboration is best witnessed during the Conservatory-wide class called the Projects. The Projects put all Conservatory students on interdisciplinary teams of twelve to fifteen artists. Teams are facilitated by a CCA/Envision teacher, and each year a new theme is selected to unite all the Projects. Examples of such themes include “Beginnings” and “Dreams.” At the end of the event, each team must present their Project (a publication, performance, exhibit, etc.) to the entire CCA school community.
</p>

