<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course will teach students the science and mechanics of digital recording in live music applications, signal processing, and audio mixing/mastering skills. Students will be using the industry standard recording software ProTools and will be learning a wide variety of recording techniques. The recording arts lab is equipped with industry standard recording hardware, software, microphones, and mobile recording stations. While a musical background is not required for this class, it is helpful. By the end of this course, students will have developed the necessary skills needed to produce professional sounding audio recordings. In addition, we have relationships with industry professionals in the audio field and regularly bring in guest speakers and take field trips to area recording studios.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This is a CTE capstone course, therefore, course standards and expectations are career and industry oriented. Students are expected to be self-motivated and independent; to meet deadlines; to work efficiently and effectively in small groups to perform, record and produce music; to research and report on industry trends and current events in audio technology.
</p>

