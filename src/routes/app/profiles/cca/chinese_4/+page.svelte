<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in Chinese 3</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Mandarin Chinese 4 will provide students a more in-depth and rigorous study of Chinese language
	and culture and prepare students for advanced Chinese study. It is an intermediate course that
	follows what students have acquired and learned from Mandarin Chinese 3. Students will continue
	to add to their knowledge of how to communicate in interpersonal situations, informal settings and
	some formal settings that include, but are not limited to the extension of classroom language,
	survival, travel, relationships, going out, events, education, internet, jobs, etc. with the
	appropriate cultural response.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will develop Chinese language skills in five areas: communication, cultures, connections,
	comparisons, and communities. In addition to reviewing fundamental sentence structures mastered
	in Chinese 1 through Chinese 3, students will further develop their skills in reading contemporary
	Chinese literature and writing essays at an intermediate level.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Throughout the course, the students are provided with numerous opportunities to engage in learning
	tasks for interpretive, interpersonal, and persuasive presentational communication. They will also
	explore aspects of Chinese culture that are related to the language under study, compare languages
	and cultures, and make connections with other subjects and communities.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Authentic Materials:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	An increased amount of authentic materials in Chinese, such as newscasts, movies, newspapers,
	lyrics, and magazines, will be used to help students advance their abilities in three language
	modes: interpersonal, interpretive, and presentational communication.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Traditional/Simplified Characters:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Chinese characters may either be written/typed in the simplified (简体) or the traditional system
	(繁體). The simplified system is used in Mainland China, while the traditional style is used in
	Hong Kong, Taiwan, and overseas Chinatowns. Students can choose to write either traditional or
	simplified Chinese characters. Textbooks will be printed in both simplified and traditional Chinese
	and the character workbooks are in both traditional and simplified Chinese.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Whenever writing characters, the teacher will write both sets of characters in all examples, text
	materials, and handouts. The aim is for all students to recognize both character sets.
</p>
