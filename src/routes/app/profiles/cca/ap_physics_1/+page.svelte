<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	AP Physics 1 is a laboratory-intensive, algebra/trigonometry-based, college-level course. AP
	Physics 1 provides students with a college-level foundation to support future advanced coursework
	in physics and prepares students for College Board AP Physics 1 exam.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students will cultivate their understanding of physics through inquiry-based experiments, as they
	explore content such as: kinematics, circular motion and gravitation, energy, momentum, fluids,
	simple harmonic motion and rotational motion.
</p>

