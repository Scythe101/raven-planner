<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is intended to introduce students to MIDI technology and digital music production. Students use industry standard Reason and Sibelius software along with a keyboard control surface to compose and produce original musical work in a variety of styles and idioms. In addition to the use of music technology, this course covers fundamental music theory and composition concepts. This course is designed for students with a deep passion for music, whether or not they have previous music theory or performance experience. While playing an instrument is not required, it is helpful.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This is a CTE concentrator course, therefore, course standards and expectations are career and industry oriented. Students are expected to be self-motivated and independent; to meet deadlines; to work efficiently and effectively in small groups to perform, record and produce music; to research and report on industry trends and current events in audio technology. This course strives to challenge artists and provide professional resources, developing artists to step up and take advantage of this opportunity.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	All assignments, including projects and portfolios, are completed in class, therefore, attendance is crucial. Grades are based on high expectations, assignments, class participation and final portfolio.
</p>
