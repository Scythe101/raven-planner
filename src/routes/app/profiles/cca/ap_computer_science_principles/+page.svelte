<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	AP Computer Science Principles offers a multidisciplinary approach to teaching the underlying principles of computation. The course will introduce students to the creative aspects of programming, abstractions, algorithms, large data sets, the Internet, cybersecurity concerns, and computing impacts. AP Computer Science Principles also gives students the opportunity to use current technologies to create computational artifacts for both self-expression and problem solving. Together, these aspects of the course make up a rigorous and rich curriculum that aims to broaden participation in computer science. At CCA we explore these topics while working with App Inventor and Android devices.
</p>
<h2 class="mt-8">Grading & Assessment</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	For the through-course assessment, you will upload digital artifacts and written responses via a web-based digital application. You will be asked to describe or analyze your work, whether it includes research, the creation of an artifact (e.g., a video, spreadsheet, graph, or electronic slide show), or the creation of a program. The end-of-course AP Exam will be a paper and pencil written exam. It will be 120 minutes long and will include approximately 74 multiple-choice questions. There are two types of multiple-choice questions: Single Select (1 answer from 4 options) and Multiple Select (2 answers from 4 options).
</p>

