<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in French 5</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The AP French Language course places an emphasis on correct pronunciation; listening
	comprehension; vocabulary development; verb conjugations in present, past, future and conditional
	tenses, both in the subjunctive and indicative moods; and the composition of French essays on a
	variety of topics requiring critical thinking.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The successful student in this advanced course will be able to write compositions on a variety of
	topics; practice spoken French by recording on an audio cassette where available; explore a
	variety of Francophone texts; and practice listening comprehension of dialogues.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">Students will study:</p>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>
		Expanded correct verb forms in present, passé composé, imparfait with an emphasis on past tenses
		used together, future and conditional tenses, the subjunctive and passé simple.
	</li>
	<li>Advanced grammar: All pronouns including object pronouns and relative pronouns.</li>
	<li>Advanced vocabulary to prepare students for class discussion and writing.</li>
	<li>Specific techniques required to succeed on the AP exam.</li>
</ul>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This class is in alignment with national standards at the intermediate-high level.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Outside of class, the student will complete exercises in advanced grammar and vocabulary in order
	to express themselves at a more sophisticated level; and write essays on a regular basis in the AP
	format. In general, the successful student will spend approximately 45-60 minutes on work outside
	of class to prepare for the next class session.
</p>

<h2 class="mt-8">Grading</h2>
<p class="text-xl 2xl:w-3/4">
	AP French Language maintains a demanding pace. Students may receive college credit either by
	success on the AP French Language exam or by the ability to place into intermediate, college
	French courses. Each semester will have a variety of tests, quizzes, oral presentations and
	compositions.
</p>
