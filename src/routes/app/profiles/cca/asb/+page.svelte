<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Leadership is a project-based course aimed at increasing students' leadership capabilities. Through the planning and execution of numerous events for the school, students will discover how to best affect change in their communities. Students will read about and engage extensively with the current best practices, research and thinking on nature of leadership and its different styles. Students will have a chance to put that knowledge into practice as they serve various roles across a number of groups to implement their vision. Leadership has two goals. One, increase the positive school culture through academic and social events. Two, help students enrolled in the class become more effective leaders.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Grading is based on performance across various categories of leadership curricular activities, event planning and execution, and individual contributions to the class and school culture. Be advised that this can include work to support activities outside of the school day (up to 2.5 hours a week). You must meet the requirements and do the assigned/expected work if you want to earn a high grade.
</p>
