<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In this course, students refine their understanding of acting as an art form, explore their
	expressive skills, and build self-awareness and confidence. Students practice improvisation, scene
	analysis, and character building techniques. These skills are developed through theatre games,
	movement exercises, improvisation, scene study, and monologue work.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course includes guest artists—professional actors and directors—who come into the classroom
	to teach in their fields of expertise.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Although students coming from other schools may have taken a beginning or intermediate acting
	course, Envision Theater faculty require that every CCA acting student begin with this course.
	Because of the highly structured Envision acting curriculum, students may not skip this course to
	move to a higher level. This course is required for Envision Theater Conservatory students.
</p>

