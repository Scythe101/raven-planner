<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	AP European History engages students in the study of European history from 1450 through the 2000,
	representative of a first-year university course. It seeks to examine the cultural, social, and
	political developments of Europe in order to discern an understanding of present-day institutions
	and "the role of continuity and change in present-day society and politics, and the evolution of
	current forms of artistic expression and intellectual discourse."
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students work to understand a brief narrative of the events of European history while at the same
	time developing an understanding of underlying themes driving those events. Like other AP history
	courses, an emphasis on primary document analysis, historical inquiry, and analysis of historical
	interpretation is constantly present. Readings from college-level textbooks and critical,
	analytical essay writing form key components of the course. The course is partly organized by the
	thematic study of cultural and intellectual history, political and diplomatic history, as well as
	social and economic history. As a benefit, students may wish to take the AP European History exam.
	Successful completion of this exam can result with students earning college credit, depending on
	the policies of individual colleges and universities.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	At Canyon Crest Academy, we have designed our AP European History curriculum with the unique
	demands of the 4x4-block curriculum in mind. Our primary objective is twofold:
</p>
<ol class="font-noto-serif 2xl:w-3/4">
	<li>To instruct students with the full content of the course in an abbreviated period of time</li>
	<li>To introduce students to various skills that are necessary for passage of the AP exam.</li>
</ol>

