<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	College Preparatory (CP) United States History fulfills both state and district requirements for
	high school graduation. The course examines the nation's history from the time of the Industrial
	Revolution to the present, with an emphasis on the Twentieth Century. Instruction is centered not
	on merely memorizing the events of American history, but on teaching students to critically
	analyze history in order to determine how the nation's past has influenced the present. Special
	attention is placed on analysis of cultural, political, and economic history—including
	technological advancements—using the District's "Big Ideas in U.S. History" as a framework.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Building from the foundations established in the CP World History course, students in CP U.S.
	History continue the process of historical document analysis, eventually completing a
	document-based essay. Additionally, students will complete a standards-based, cross-curricular
	research project based on the content of U.S. History. Occasionally, the study of current events
	in American society will be infused through the curriculum in order to highlight connections to
	standards.
</p>

