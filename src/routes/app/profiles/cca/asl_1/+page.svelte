<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">None</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed to help the non-signer and/or the beginning signer develop basic skills
	and knowledge of American Sign Language and Deaf culture.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is an introduction to basic knowledge of American Sign Language (ASL) and the Deaf
	culture. Students will develop basic skills in ASL vocabulary and grammar to meet basic
	communication needs. Emphasis is placed upon acquisition of both comprehension and production
	skills. In addition to language, learning will focus upon knowledge of the Deaf community and the
	development of the cultural awareness necessary for communication and interaction.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will be able to introduce themselves, conduct basic getting-to-know-you conversations,
	greet and say goodbye in a culturally appropriate manner, exchange personal information, talk
	about surroundings, tell where they live, talking about their families, tell about activities and
	appropriate ways to get one's attention in ASL.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Themes:</h3>
<p class="mr-4 text-xl 2xl:w-3/4">
	Themes include questions, identity, classroom environment, food, directions, housing,
	transportation, family and other relationships, possessives, negotiating the environment,
	negation, time and opinions.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Grammar Topics:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Grammar topics include yes/no and why-question formats; confirming and correcting; non-manual
	markers; real-world orientation; ordinal/cardinal numbers and time signs; noun-verb pairs;
	contrastive structure; personal, possessive and dual pronouns; negation sentence types; role
	shifting, introduction to conditional clauses and introduction to classifiers.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Student Outcomes:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The successful student will be able to sign introductions, exchange personal information, describe
	surroundings, describe where he/she lives, express politeness, make requests, sign about family
	and friends, express herself/himself, describe others and discuss daily activities, food and
	shopping. Typical activities include signing and fingerspelling (FS) warm-up; reviewing and
	learning new vocabulary; practicing expressive and receptive skills through signed dialogues;
	hand-outs; taking notes on ASL grammar, sentence structure and Deaf culture; playing ASL
	vocabulary review games.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Upon completion of this course, students will be able to manage common interactions using basic
	vocabulary and grammar; continue to apply language-learning skills outside the language classroom;
	act with respect and a better understanding of Deaf people with an appreciation for their
	linguistic and cultural diversity.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	This class is in alignment with the national standards at the novice level.
</p>

<h2 class="mt-8">Grading</h2>
<p class="text-xl 2xl:w-3/4">
	Outside of class, the successful student will spend approximately 30 minutes reviewing class notes
	and practicing vocabulary and fingerspelling. Students will also spend time working on projects,
	presentations and Deaf culture assignments.
</p>
<p class="text-xl 2xl:w-3/4">
	The majority of a student's grade will be determined by exams, quizzes, class work, homework,
	projects, expressive and receptive signing skills and class participation. However, the following
	scale is used to determine the final grade:
</p>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>A: 100% to 90%</li>
	<li>B: 89% to 80%</li>
	<li>C: 79% to 70%</li>
	<li>D: 69% to 60%</li>
	<li>F: 59% and below</li>
</ul>
