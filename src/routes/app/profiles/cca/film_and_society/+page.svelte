<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	This class will examine the relationship between society and the films that have been made.
	Students will learn about the different types of film genres and how movies are produced. Students
	will learn about historical events and Hollywood is represented them on the big screen. Students
	will have a deeper appreciation of how film has been an important tool to educate people on the
	struggles and conflicts that have occurred throughout history. Students will also have the
	opportunity to write about what they feel are important struggles in society and share their
	opinions in daily discussions.
</p>

