<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Cinema Lab is a course that is offered to a small group of students (1-4 per period) embedded within all of the other Envision Cinema day classes (Video/Film, Advanced Video/Film, ROP Media Production). These students work on independent projects and prepare portfolio work for college and career opportunities in the video, film, and television industry. Students are selected for the course through an application process.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will explore, create, evaluate, and criticize cinema as an art form. They will also develop skills for cinematography, editing, scriptwriting, equipment organization and maintenance, leadership, marketing, and public relations. Cinema Lab students help with equipment management and serve as mentors for students in the introductory classes. Students in Cinema Lab will also organize and provide leadership for campus film festivals and a campus-wide television program. Using the art elements of form, color, and value, they will create and communicate meaning through video and film production.
</p>

