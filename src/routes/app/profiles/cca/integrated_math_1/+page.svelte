<h2 class="mt-8">Student Background</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students entering Integrated Math 1 should <strong>already</strong> have a good understanding of the
following concepts:
</p>
<ul class="font-noto-serif mr-4 ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Understanding of radicals and integer exponents</li>
	<li>Understanding the connection between proportional relationships, lines, and linear equations.</li>
	<li>Solving linear equations as well as apply graphical and algebraic methods to analyze and solve systems of linear equations in two variables.</li>
	<li>Defining, evaluating, and comparing functions, and use them to model relationships among quantities.</li>
	<li>Understanding of the rigid motions: translations, reflections, and rotations.</li>
	<li>Understanding congruence and similarity using physical models, transparencies, or geometry software.</li>
	<li>Understanding and apply the Pythagorean Theorem</li>
	<li>Solving real-world and mathematical problems involving volume of cylinders, cones, and spheres.</li>
	<li>Identifying patterns of association in bivariate data.</li>
</ul>
<h2 class="mt-8">Course Content and Expectations</h2>
<p class=" mr-4 text-xl 2xl:w-3/4">
	In Integrated Math 1, students will learn concepts such as:
</p>

<ul class="font-noto-serif mr-4 ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Manipulating algebraic expressions including rearranging and collecting terms</li>
	<li>Solving and understanding equations and inequalities as a process of reasoning and explaining the reason.</li>
	<li>Understanding the concept of a function and use function notation, domain, and range.</li>
	<li>Interpreting functions given graphically, numerically, symbolically, and verbally.</li>
	<li>Modeling and analyzing various representations of functions and understanding their limitations.</li>
	<li>Modeling with functions using tables, functions, and understanding when the context allows for a model that is only an approximation.</li>
	<li>Constructing and comparing and linear and exponential models to solve real-life problems.</li>
	<li>Looking at arithmetic sequences as linear functions and geometric sequences as exponential functions.</li>
	<li>Writing, interpreting, and translating among various forms of linear equations and inequalities.</li>
	<li>Summarizing, representing, and interpreting data on a single count or measurement variable and on two categorical and quantitative variables.</li>
	<li>Using regression techniques to describe relationships among quantities and look at residuals to analyze the model and make predictions.</li>
	<li>Understanding triangle congruence criteria based on analyses of rigid motions and formal constructions.</li>
	<li>Using a coordinate system to verify geometric relationships and apply the Pythagorean Theorem to find distances.</li>
</ul>
