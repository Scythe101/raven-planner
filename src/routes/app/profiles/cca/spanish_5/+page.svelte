<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in Spanish 4</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed as a pre AP® Spanish Language and Culture course taught exclusively in
	Spanish and requires students to improve their proficiency across the three modes of communication
	(interpersonal, presentational, and interpretive). The course focuses on three AP Spanish Language
	and Culture themes through authentic resources—online print, audio, audiovisual, traditional
	literature, essays, magazine and newspaper articles, and visual resources such as charts, tables,
	and graphs—to provide a diverse learning experience. Students communicate using rich, advanced
	vocabulary and linguistic structures as they build proficiency toward the pre-AP advanced level.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This class prepares students to communicate in real-life situations. Vocabulary expansion is
	organized by daily life topics, reinforced through exercises aligned with thematic chapters.
	Source materials include major works of literature (short stories, plays, poetry, novel extracts),
	cultural readings, newscasts, music, and films. Intensive grammar practice, vocabulary review, and
	fine-tuning of written and oral expression raise overall proficiency and fluency.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Description of a Spanish 5 student:</h3>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>
		The student actively negotiates meaning in Spanish conversations and interprets cultural nuances
		in written texts and audio.
	</li>
	<li>
		The student has a strong command of Spanish linguistic skills supporting effective
		communication.
	</li>
	<li>The student comprehends Spanish intended for native speakers in various settings.</li>
	<li>The student is able to interpret information from authentic Spanish sources.</li>
	<li>
		The student recognizes cultural perspectives of their community and the Spanish-speaking world.
	</li>
</ul>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Workload:</h3>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Frequent journal writings in response to prompts or readings</li>
	<li>
		Formal oral presentations comparing cultural features of local and Spanish-speaking communities
	</li>
	<li>Reading from newspapers, magazines, internet articles, short stories, essays, and poetry</li>
	<li>Formal and informal writing: persuasive essays, letters, postcards, and e-mails</li>
	<li>Informal conversations, interviews, skits, and dialogues in Spanish</li>
	<li>Listening to narratives, dialogues, interviews, and other authentic sources</li>
	<li>Review and practice of grammatical structures in context</li>
	<li>
		Participation in coherent Spanish conversations through class discussions and group activities
	</li>
</ul>

<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Outside of class, students complete assignments that reinforce class skills and review notes. In
	general, the successful student will spend approximately 30-60 minutes on work outside of class to
	prepare for the next session.
</p>

