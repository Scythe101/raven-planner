<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Envision Visual Arts (EVA) Conservatory is an audition-based, after-school extension of the
	daytime Envision program, offering smaller class sizes and more individualized instruction for the
	most advanced art students.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students in EVA Conservatory study figure drawing, painting, mixed media, sculpture, digital
	imaging, digital photography, graphic design, and drawing. They gain a broad foundation in the
	digital and fine arts while developing a personal aesthetic and preparing a portfolio for
	postsecondary study. Students work under close mentorship of teaching artists. Students in the EVA
	Conservatory also work on a variety of public art projects, such as ArtWalk murals, Ray St, Public
	art projects, and Gesso Italiano. There are also on campus shows and events such as 24 Hr Art In,
	Festival of the Arts, and EVA.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The collaborative nature of the arts ensures that Conservatory students enjoy many opportunities
	to work with and learn from one another. A focus on interdisciplinary collaboration is best
	witnessed during the Conservatory-wide event, Projects. Projects put all of the Conservatory
	students on interdisciplinary teams of twelve to fifteen artists. Teams are facilitated by an
	Envision teacher, and each year a new theme is selected to unite all the Projects. Examples of
	such themes include “Beginnings” and “Dreams”. At the end of the weekend, each team must present
	their Project (a publication, performance, exhibit, etc.). Collaborations, such as Spirit Skies,
	may span several years and students work on a variety of angles and approaches.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Public school art is a large component of EVA Conservatory and students learn to work with
	proposals and rewrites for a final project. Seniors are in charge of putting on their own group
	thesis art show.
</p>

