<h2 class="mt-8">Student Background</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students entering Integrated Math 2 Honors should easily grasp higher level concepts and embrace
	rigorous curriculum. Students should <strong>already</strong> have mastered the following concepts:
</p>
<ul class="font-noto-serif mr-4 ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Working with radicals and integer exponents</li>
	<li>Understanding the connection between proportional relationships, lines, and linear equations.</li>
	<li>Solving linear equations as well as apply graphical and algebraic methods to analyze and solve systems of linear equations in two variables.</li>
	<li>Understanding arithmetic/geometric sequences and their relationship to linear/exponential functions.</li>
	<li>Defining, evaluating, and comparing functions, and use them to model relationships among quantities.</li>
	<li>Understanding congruence and similarity using transformational geometry.</li>
	<li>Solving real-world and mathematical problems using linear, exponential and quadratic mathematical models.</li>
	<li>Solving quadratic equations using different methods.</li>
	<li>Graphing quadratic functions and fluently translate functions between different forms to identify key features of the function.</li>
	<li>Understanding, modeling, and performing arithmetic on vectors, matrices and complex numbers.</li>
	<li>Simplifying expressions with rational exponents.</li>
</ul>
<h2 class="mt-8">Course Content and Expectations</h2>
<p class=" mr-4 text-xl 2xl:w-3/4">
	In Integrated Math 2 Honors, students will go deeper into grade level standards as well as several Pre
Calculus and Integrated Math 3 standards. Student assignments will contain more critical thinking and have a
higher depth of knowledge and more performance tasks. In this accelerated course, students will learn
concepts such as:
</p>

<ul class="font-noto-serif mr-4 ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Manipulating algebraic expressions including rearranging and collecting terms, factoring, and applying properties of exponents</li>
	<li>Solving and understanding quadratic equations and inequalities.</li>
	<li>Understanding the concept of a function and use function notation, domain, and range.</li>
	<li>Interpreting functions given graphically, numerically, symbolically, and verbally.</li>
	<li>Modeling with functions using tables, functions, and understanding when the context allows for a model that is only an approximation.</li>
	<li>Writing, interpreting, and translating among various forms of quadratic equations and inequalities.</li>
	<li>Graphing and analyzing absolute-value functions and piece-wise functions.</li>
	<li>Experimenting, conjecturing and proving properties of triangles, quadrilaterals, polygons and circles.</li>
	<li>Using similarity to define and solve problems using right-triangle trigonometry.</li>
	<li>Using a coordinate system to analyze properties of circles, parabolas, ellipses, and hyperbolas.</li>
	<li>Using the unit circle and radians to extend trigonometry to any angle.</li>
	<li>Simplifying, graphing, and examining the structure of inverse functions and logarithmic functions.</li>
	<li>Proving and applying the properties of logarithmic functions.</li>
	<li>Performing arithmetic on complex numbers.</li>
	<li>Finding probability of independent, dependent and conditional events by experimentation, theoretical model, two-way tables, Venn diagrams and tree diagrams.</li>
</ul>
