<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Theatre Conservatory is an audition-based, after-school extension of the daytime Envision program, offering smaller class sizes and more individualized instruction for the most advanced theatre students. This conservatory aims to graduate theatre artists with a broad understanding of theatre, a variety of technical skills, and an ability to articulate their own creative process.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Ensemble work and professionalism are core values of this course that are often referenced and revisited.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Theatre Conservatory exposes a small company of self-motivated, mature student artists to a broad range of theatrical skills, experiences, and information. Students participate in workshops, lectures, volunteer work, and performances. Conservatory work explores a broad range of theatrical genres and workshops are offered in different areas of study, including theatrical design, acting styles, voice and body work, and directing.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The collaborative nature of the arts ensures that Conservatory students enjoy many opportunities to work with and learn from one another. A focus on interdisciplinary collaboration is best witnessed during the three-week, Conservatory-wide class called the Projects. The Projects put all Conservatory students on interdisciplinary teams of twelve to fifteen artists. Teams are facilitated by a CCA/Envision teacher, and each year a new theme is selected to unite all the Projects. Examples of such themes include “Beginnings” and “Dreams.” At the end of the three weeks, each team must present their Project (a publication, performance, exhibit, etc.) to the entire CCA school community.
</p>

