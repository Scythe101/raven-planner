<h2 class="mt-8">Student Background</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students entering Integrated Math 3 should already have a good understanding of the
following concepts:
</p>
<ul class="font-noto-serif mr-4 ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Understanding properties of linear, exponential and quadratic functions.</li>
	<li>Graphing and describing features of linear, exponential and quadratic functions.</li>
	<li>Defining, evaluating, and comparing functions, and use them to model relationships among quantities.</li>
	<li>Knowing and proving properties of lines, triangles, quadrilaterals and circles.</li>
	<li>Solving real-world and mathematical problems using linear, exponential, and quadratic mathematical models.</li>
	<li>Understanding and applying right triangle trigonometry and the special right triangles.</li>
	<li>Understanding and calculating probability for independent and dependent events.</li>
	<li>Understanding unit circle trigonometry and radian measures of angles.</li>
	<li>Graphing and modeling with logarithmic functions.</li>
	<li>Proving and applying properties of logarithmic functions and inverse functions.</li>
</ul>

<h2 class="mt-8">Course Content and Expectations</h2>
<p class=" mr-4 text-xl 2xl:w-3/4">
	In Integrated Math 3, students will learn concepts such as:
</p>

<ul class="font-noto-serif mr-4 ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Manipulating polynomial and rational expressions.</li>
	<li>Graphing polynomial and rational functions and identifying key features of the graphs (turn-around points, end behavior, asymptotic behavior, etc.)</li>
	<li>Understanding the concept of inverse functions, domain, and range, and identify properties of functions and graphs.</li>
	<li>Interpreting and composing functions given graphically, numerically, symbolically, and verbally representations.</li>
	<li>Modeling with functions using tables, functions, and understanding when the context allows for a model that is only an approximation.</li>
	<li>Creating and modeling real-life scenarios with rational functions, logarithmic functions, and trigonometric functions.</li>
	<li>Discovering and proving properties of logarithmic functions.</li>
	<li>Extending right-triangle trigonometry to the unit circle.</li>
	<li>Graphing trigonometric functions and identifying key features.</li>
	<li>Proving and applying the Law of Sines and Law of Cosines.</li>
	<li>Using statistics and the standard normal curve to make inferences on a population.</li>
</ul>
