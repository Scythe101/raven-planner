<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	One of the last courses in the Social Science curriculum, College Preparatory Economics is
	designed to introduce students to the basic concepts of both microeconomic and macroeconomic
	principles. It is a rigorous course intended to provide the student with the vocabulary of
	economics, the skills necessary to understand and analyze the current economic issues and an
	appreciation of the economic system that has evolved in the U.S. and in the world today.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	This course is aligned to California academic standards for U.S. Government. There are
	approximately six units of study:
</p>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Common Economic Terms and Concepts and Economic Reasoning</li>
	<li>Elements of America's Market Economy</li>
	<li>Relationship of the Federal Government with the American Economy</li>
	<li>Elements of the U.S. Labor Market and the Global Market</li>
	<li>Aggregate Economic Behavior</li>
	<li>International Trade and Global Market Forces</li>
</ul>
<p class="mr-4 text-xl 2xl:w-3/4">
	Typical assignments in this course may include assessment of current events by watching television
	news programs, visiting news organization websites, or reading the newspaper. Additionally,
	students may be expected to complete independent projects as a requirement of the course. Examples
	of this might include the development of a business plan, the analysis of stock market
	performance, analysis of the national budget, or a research paper on an economic topic.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Due to the expedited nature of a 4x4 course, students can expect to read approximately two-to-
	three chapters per week. There are approximately six units anticipated. Students can expect a
	weekly test.
</p>

