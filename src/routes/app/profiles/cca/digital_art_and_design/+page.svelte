<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">Drawing & Design or Photography HIGHLY recommended</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Digital Art and Design is an introductory course that teaches students a broad set of skills in
	Photoshop and Illustrator while maintaining a fine arts approach and focus. The course will
	introduce how to work with basic raster and vector-based images while emphasizing specific
	skills/techniques as well as individual creativity. Students investigate current trends and
	careers in technology as they explore innovative and conceptual approaches to art making.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Formal design standards and principles will be emphasized along with creative problem solving,
	experimentation, and personal expression. Projects include pattern creation, photo compositing and
	manipulation, digital drawing and painting, typography and logo design, and vector illustration.
	Some of the art making includes the crossover and interplay between digital and traditional art
	forms.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Digital Art and Design course is built to foster students' basic understanding of digital
	image making and design processes. Utilizing digital photography, Photoshop, and Illustrator via
	multiple projects and workshops that strive to have real world connection, students will develop
	their skill and understanding of how to manipulate images digitally. Moreover, this course and the
	subsequent Advanced Digital Art and Design, will also engage students in developing a critical eye
	and understanding of how images and design work on us in culture, and how images and design are
	used to entertain, inform, and influence.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In an effort to provide a professional working atmosphere the grade in imaging is based on a set
	minimum standard and expectation of work determined at the project level. The course grade
	manifests in a digital portfolio of work coupled with written grade proposals based on students'
	work above and beyond minimum standard set.
</p>
