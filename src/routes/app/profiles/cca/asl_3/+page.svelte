<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in ASL 2</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course expands on ASL vocabulary and grammar to develop communicative and interactive
	competencies in the language and culture of Deaf people. ASL 3 emphasizes refinement of the
	fundamental comprehension and production skills covered in ASL 2, with the acquisition of
	additional vocabulary and grammatical structure. In addition to learning the language, this course
	will focus upon the knowledge of the Deaf community and the development of the cultural awareness
	necessary for communication and interaction.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Course content includes locating things around the house; complaining, making suggestions and
	requests; exchanging personal information; life events; and daily situational dialogues.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Themes:</h3>
<p class="mr-4 text-xl 2xl:w-3/4">
	Themes include the house, objects, quantifiers, numbers, ailments, suggestions, remedies, empathy,
	time signs (recurring & continuous), complaints, requests, nationality signs and other ASL
	expressions.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Grammar Topics:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Grammar includes topic-comment structure; weak hand as reference point; SASS, entity and handling
	classifiers; yes/no & why-questions; numbers (ordinal, clock, dates and addresses); recurring &
	continuous time signs; relative time; temporal aspect; inflecting verbs; role shifting;
	conditional and declarative sentences; conditional clauses; phrasing and sequencing events;
	contrastive structure; possessive forms; non-manual markers; pronominalization; subjects and
	objects; pluralization; and distributional aspect.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Student Outcomes:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Upon completion of this course, students will be able to manage more complex interactions using
	expanded ASL vocabulary and grammar; continue to apply language learning skills outside the
	language classroom; act with respect, knowledge and understanding of Deaf people and ASL with an
	appreciation for their linguistic and cultural diversity; and exhibit mastery of the target
	language at the intermediate level.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	This course is in alignment with national standards at the intermediate level.
</p>

<h2 class="mt-8">Grading</h2>
<h3 class="font-dm-serif-display mt-4 text-2xl italic">Workload:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will be expected to spend an average of approximately 30 minutes to an hour outside of
	the classroom for each class period studying vocabulary, grammar rules and fingerspelling.
	Students will also spend time working on projects, presentations and Deaf culture assignments.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Assessment:</h3>
<p class="text-xl 2xl:w-3/4">
	The majority of a student's grade will be determined by exams, quizzes, class work, homework,
	projects, expressive and receptive signing skills, and class participation.
</p>
