<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This is an upper-intermediate course in Spanish. Instruction is 100% in Spanish, and students
	will be expected to communicate with the teacher and peers entirely in Spanish. The aim of the
	course is for students to reach at least 80% accuracy and fluency by the end of the term.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This class aligns with national standards at the intermediate level, as well as AP Spanish Language
	and Culture themes (global challenges; science and technology; contemporary life; personal and
	public identities; families and communities; beauty and aesthetics). Students will further develop
	grammar, reading, writing, speaking, and listening skills while deepening cultural awareness.
	Intermediate and advanced grammar is reviewed and reinforced to support near-native fluency.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In reading comprehension, students learn to approach, understand, and interpret authentic texts
	from diverse sourcesâ€”literature, essays, magazines, music, scientific and news articles. In written
	communication, they compose short pieces, reflective journal entries, and coherent three-paragraph
	essays.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Typical activities include analyzing advanced vocabulary, reading short stories and articles,
	practicing listening with authentic audio, applying grammar in conversation, writing journal entries,
	and completing cultural research projects.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Outside of class, students will review readings, complete comprehension exercises, learn new
	vocabulary, and thoughtfully complete written assignments and projects. In general, they will spend
	approximately 30-60 minutes on review and homework per session.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
