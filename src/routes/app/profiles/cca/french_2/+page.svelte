<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in French 1</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Successful students will enter this course with a knowledge of present-tense verb conjugations,
	noun/adjective agreement, definite and indefinite articles and thematic vocabulary presented in
	French 1.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students should be able to write a brief paragraph on a given topic (description of yourself and your
	surroundings, school and weekend activities) and comfortably answer basic questions orally in
	French.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The French 2 course places an emphasis on correct pronunciation, listening comprehension,
	vocabulary development, the fundamentals of verb conjugations in present and past tense, and the
	formation of simple, complete and grammatically correct French sentences.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Content:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will learn concepts such as expanded vocabulary for talking about home, friends, family,
	school, entertainment, travel; correct verb forms in present, passé composé, imparfait with an
	emphasis on past tense and an introduction to future and conditional tenses; possessive
	adjectives; object pronouns; and an appreciation for and understanding of the French-speaking
	culture in France and some of the other 35 French-speaking nations, including a unit of study on
	Africa.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	This class is in alignment with national standards at the novice-high level.
</p>

<h2 class="mt-8">Grading</h2>
<h3 class="font-dm-serif-display mt-4 text-2xl italic">Workload:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will be expected to spend an average of approximately 30 minutes to an hour outside of
	class on homework for each class period.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Assessment:</h3>
<p class="text-xl 2xl:w-3/4">
	Exams, quizzes, projects, writing assignments, oral participation in class and homework.
</p>
