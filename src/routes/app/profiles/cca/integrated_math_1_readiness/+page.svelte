	<li>Performing operations with positive and negative rational numbers.</li>
	<li>Understanding the use of variables in mathematical expressions and equations.</li>
	<li>Writing, interpreting, and using expressions, equations, and inequalities that correspond to given situations and solve problems.</li>
	<li>Finding common factors and multiples.</li>
	<li>Having a basic understanding of measure of central tendency for data sets.</li>
