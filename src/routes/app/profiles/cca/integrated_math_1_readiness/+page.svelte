<h2 class="mt-8">Course Content and Expectations</h2>
<p class=" mr-4 text-xl 2xl:w-3/4">
	In Integrated Math 1 Readiness, students will learn concepts to prepare them for Integrated Math 1 such as:
</p>

<ul class="font-noto-serif mr-4 ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Solving and understanding multiple step equations and inequalities</li>
<li>Working with functions and understand their relationships as well as modeling with functions</li>
<li>Solve linear equations/inequalities and systems of equations/inequalities using various methods.</li>
<li>Understanding how figures behave under translations, reflections, dilations, and rotations.</li>
<li>Combine positive and negative like terms</li>
<li>Review concepts about lines, angles, and quadrilaterals.</li>
<li>Using a coordinate system to verify geometric relationships.</li>
<li>Representing and analyzing quantitative relationships between dependent and independent variables.</li>
<li>Develop understanding of statistical variability.</li>
<li>Summarizing and describing numerical data sets and distributions, identifying clusters, peaks, gaps, and symmetry, considering the context in which the data was collected.</li>
</ul>
