<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in Chinese 5</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	AP Chinese Language and Culture is an advanced academic course that that follows what students
	have acquired and learned from Mandarin Chinese 5 and will provide students a wider, more in-depth
	and rigorous study of Chinese language and culture and prepares students for the Advanced
	Placement exam.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will continue to increase their knowledge and proficiency level of communication in
	interpersonal situations, informal settings, and formal settings that include, but are not limited
	to, personal and public identities, science and technology, global challenges, families and
	communities, beauty and aesthetics, and contemporary life, etc. with their appropriate cultural
	responses, and expand their knowledge of other disciplines. Not only will students develop and
	advance their Chinese communication proficiency, they will also develop their understanding of
	cultural influences, connections, contexts, and distinctive viewpoints and in their global
	competence.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Throughout the course, students are provided with a variety of opportunities to engage in learning
	tasks for interpretive, interpersonal, and persuasive presentational communication. They will also
	explore aspects of Chinese culture that are related to the language under study, compare languages
	and cultures, and make connections with other subjects and communities. Authentic materials in
	Chinese, such as podcasts, movies, online newspapers and articles, song lyrics, and magazines will
	be used to help students advance their Chinese language proficiency level.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Chinese characters may either be written/typed in the simplified or the traditional system. The
	simplified system is used in Mainland China, while the traditional style is used in Hong Kong,
	Taiwan, and overseas Chinatowns. Students can choose to write either traditional or simplified
	Chinese characters. Textbooks will be printed in both simplified and traditional Chinese and the
	character workbooks are in both traditional and simplified Chinese. Both sets of characters will
	be provided for all examples, text materials, and handouts.
</p>

