<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	English 11 is a one-term course which allows students to explore the theme "To Be an American" by
	studying classic and modern American works—novels, short stories, poems, dramatic works, and
	essays & expository writings. Students will study and analyze the author's purpose and methodology
	for creating his/her work—including the historical background and the societal influence. Students
	will write essays and will practice timed writings in order to prepare them for the SAT, ACT, EAP
	and the UC and CSU English Placement Tests. Students will use critical thinking skills—as modeled
	by their teacher—and will apply them to assignments independently and in cooperative-learning
	groups. Students will be challenged to grow through class discussion, writing assignments,
	creative projects, dramatizations, oral presentations and writing workshops. All juniors will have
	the opportunity participate in the Early Assessment Program, which elicits an analytical response
	to non-fiction text. The writing focus is designed to prepare students for college level writing
	requirements.
</p>

