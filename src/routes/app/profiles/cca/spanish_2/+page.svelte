<h2 class="mt-8">Grading</h2>
<h3 class="font-dm-serif-display mt-4 text-2xl italic">Workload:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In and out of class, students will study, prepare for quizzes, practice grammar concepts, prepare flashcards to aid in acquiring new vocabulary, watch Spanish-language television, write paragraphs on various topics, participate orally, illustrate concepts, participate in group activities, listen to music and other audio sources. Outside of class, the student will review notes, practice vocabulary and thoughtfully complete written assignments. In general, the successful student will spend approximately 25-35 minutes on work outside of class to prepare for the next class session.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Assessment:</h3>
