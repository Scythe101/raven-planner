<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in Spanish 1</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The successful student should enter this course with strong understanding of these concepts and skills: grammar (subject pronouns; definite and indefinite articles; noun-adjective agreement; present-tense verb conjugation; expressions with "tener"; “ir a . . .”; possessive adjectives), vocabulary (numbers, dates, time, greetings, expressions of politeness, seasons, weather, family, friends, home, food, meals), conversation (greetings, expressing polite comments, telling the time and date, describing family) and writing (able to write a beginning-level paragraph accurately in the present tense).
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This beginning-level course should be attempted after Spanish 1 has been successfully completed. In this course the student will review some skills from Spanish 1 and acquire new skills in the areas of grammar, vocabulary, reading, writing, comprehension and culture. Cultural components include, but are not limited to, Hispanic cultures in the United States and South America. The foundational grammar concepts emphasized in this class will foster success in intermediate courses. The successful student in this continuing course will be able to understand and convey information about himself/herself and the world around him/her in the present tense and past tense; and give and understand directions. This class is in alignment with national standards at the novice-high level.
</p>

