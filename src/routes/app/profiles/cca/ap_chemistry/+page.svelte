<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">Successful completion of Honors Chemistry: In the Earth System (B or better)</p>
<p class="text-xl"><strong>AND</strong></p>
<p class="text-xl">Completion or co-enrollment of Integrated Math 2</p>

<h2 class="mt-8">Recommendations</h2>
<p class="text-xl">
	Students interested in taking AP Chemistry are encouraged to take Honors Chemistry: In the Earth
	System fall semester and AP Chemistry spring semester in the same school year.
</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	AP Chemistry is a laboratory-intensive course for students who have successfully completed a
	first-year chemistry course (honors recommended) and are considering careers in science,
	technology, engineering and/or mathematics (STEM). AP Chemistry provides students with a
	college-level foundation to support future advanced coursework in chemistry and prepares students
	for College Board AP Chemistry exam.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students will cultivate their understanding of chemistry through inquiry-based experiments, as
	they explore content such as: atomic structure, intermolecular forces and bonding, chemical
	reactions, kinetics, thermodynamics and equilibrium.
</p>

