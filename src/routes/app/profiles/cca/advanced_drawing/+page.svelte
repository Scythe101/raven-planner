<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed for students who have successfully completed the introductory level
	Drawing &amp; Design class and would like to continue to develop their drawing skills at a more
	advanced level. We will focus on further mastering technical skills and in-depth knowledge of 2D
	art concepts like line quality, mark-making, perspective, illusions of depth and form, and surface
	manipulation. Students will be encouraged to experiment and explore creative risk-taking while
	integrating a more deliberate personal voice and style into their work. Additionally, we will look
	at influential contemporary and historical artists for inspiration and to better understand the
	context of artmaking throughout history. This is an ideal course for any students interested in a
	class that will prepare them for a more successful experience in Conservatory, AP Studio Art,
	college and/or career in the visual arts.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This is a studio-based course meaning that most work will be completed during class time and will
	be project based. This course requires good attendance, a strong work ethic, and willingness to
	try new things every day. Because this is an advanced level course, students can expect to spend
	additional time outside of class to successfully complete projects as needed. Grading for Advanced
	Drawing classes typically consists of projects, studio performance, and classwork. Specific
	weighting of assignments is determined by each individual instructor.
</p>
