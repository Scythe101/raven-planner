<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	AP 3D provides an environment for the dedicated art student to create a college-level three dimensional sculptural portfolio, referred to as a ‘sustained investigation’ that shows inquiry, investigation, practice, experimentation, revision, communication, and reflection. This portfolio is intended to address three-dimensional design issues and purposeful decision making about how to use the elements and principles of design in an integrative way. Requirements for the final portfolio are developed by the College Board Advanced Placement Program, including a Selected Works and a Sustained Investigation Section that consists of advanced level artworks, including written documentation.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Through a series of assignments and independent-study projects, students will demonstrate mastery of 3-D design skills by exploring a variety of concepts and approaches before beginning a sustained investigation. This is an advanced level course that is designed for students who want to develop beyond mastery in their art making skills. The course is developed as a college level course completed at the high school level. The expectations and grading are that of a college level course. This course is rigorous and will require full participation both in and out of the classroom.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students are graded in two main areas: <strong>Projects</strong> and <strong>Participation</strong>
</p>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>
		<strong>Projects:</strong> This may include, but is not limited to: craftsmanship, application of skill, use of classroom time, growing and improving artistic skills, photographing, and submitting completed assignments. Projects are graded using the rubric created by the College Board Advanced Placement Program.
	</li>
	<li>
		<strong>Participation:</strong> This may include, but is not limited to: sketches, video/article reviews, class critiques, self reflections, portfolio building, studio maintenance/safety, and group discussions.
	</li>
</ul>
