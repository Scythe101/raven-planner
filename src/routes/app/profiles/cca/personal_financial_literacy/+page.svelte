<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	This course assists students in gaining the knowledge, tools, attitude, and skills needed to make informed lifelong financial decisions that will empower their lives. Students explore the social, psychological, and physiological issues related to planning and managing a personal financial plan. Topics include goal setting, budgeting, money management, taxes, savings, consumer credit, automobiles, housing, insurance, investment vehicles, retirement and estate planning, and career planning.  For a final project, students will develop a complete financial plan. 
