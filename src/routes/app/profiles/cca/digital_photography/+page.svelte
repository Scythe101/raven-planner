<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Think of Digital Photography as a traditional art class (like Painting) where the tools are different: instead of canvas and paint, we use cameras, computers, software programs, and printers. The emphasis is on approaching the world of photography as a means of artistic expression (this is different than "photojournalism" or taking "snapshots.")
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Digital Photography is designed for students who are interested in fine art photography with digital tools. General computer experience is expected, but no prior knowledge of the particular software programs used in the class is required. Because of the project-based nature of the class, students at all levels will be able to learn at their own pace and create meaningful pieces to add to their portfolio.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Visual Arts Standards are infused in the Digital Photography curriculum. You will start by "learning to really see photography" (artistic perception and aesthetic valuing) and "studying the masters of photography" (historical and cultural context). Throughout the photographic projects that you complete (creative expression), you are constantly evaluating the artistic skill, both aesthetics and expression, of your own and other's work (aesthetic valuing) and placing the work in a broader context (connections, relationships, and applications and historical and cultural context).
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This class requires a very strong work ethic in class. There is very little homework and all the photography and computer work is done during class, so it is imperative that the student can stay focused during class time. Students are required to be focused on photography for the entire class period.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is articulated with MiraCosta College Art 251 if standards are met by student (application to MiraCosta College, grade of B or better in course and Final Examination).
</p>

