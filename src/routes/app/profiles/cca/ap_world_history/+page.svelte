<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	The AP World History course is designed to prepare students for the content demands of the AP
	World History curriculum, offered by the College Board. The course follows the standards
	prescribed in the AP Course description:
	https://apcentral.collegeboard.org/pdf/ap-world-history-course-and-exam-description.pdf
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students study history from a global perspective spanning from the Foundations period (8000
	B.C.E.) through current day. The course is organized around key themes and use of historical
	thinking skills. Students read nightly and learn to analyze primary-source documents. Successful
	completion of this exam can result with students earning college credit, depending on the policies
	of individual colleges and universities. At CCA, we have designed our AP World History curriculum
	with the unique demands of the 4x4 block curriculum in mind. Our primary objective is twofold:
</p>
<ol class="font-noto-serif ml-6 list-outside list-decimal text-xl 2xl:w-3/4">
	<li>To instruct students with the full content of the course in an abbreviated period of time</li>
	<li>To introduce students to various skills that are necessary for passage of the AP exam</li>
</ol>

