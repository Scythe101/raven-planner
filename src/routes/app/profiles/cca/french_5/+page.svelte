<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in French 4</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The successful student in this advanced course will be able to listen to and discuss news casts in
	French; express opinions and carry on conversations on social and cultural issues; read and
	comprehend excerpts of literary works independently; and begin to write compositions in which they
	organize and defend their ideas. This class is in alignment with national standards at the
	intermediate-mid level.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Activities:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Typical activities include watching and analyzing film excerpts; creating a short film in French;
	watching news clips and reading current-events articles and using them as a springboard for class
	discussion and writing.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Outside of class, the student will complete exercises in advanced grammar and vocabulary in order
	to express themselves at a more sophisticated level. In general, the successful student will spend
	approximately 30-60 minutes on work outside of class to prepare for the next class session.
</p>

