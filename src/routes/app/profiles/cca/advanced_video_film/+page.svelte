<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Advanced Video/Film is an Envision Cinema class that provides students with hands-on experiences
	in video production. This course is designed for students who have completed the introductory
	Video/Film course and have an interest in the television, video, film, or other communication
	professions. Students in the class work together as a real-world production studio to film and
	edit video content largely geared toward, but not limited to, our weekly campus broadcast, CCA-TV,
	which covers various community activities and stories.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	As an SDUHSD Career Technical Education (CTE) course, Advanced Video/Film prepares students with
	skills that translate directly to future working opportunities in the film industry and for
	further study at the college level.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students can earn college credit in Advanced Video/Film through articulation (credit by exam) with
	Palomar College's DBA 120. This is a 3-unit course. The course introduces theory, terminology,
	practices, and aesthetic considerations of visual and sound productions in a multi-camera
	television studio and control room. Topics include studio signal flow, directing, theory and
	operation of camera and audio equipment, switcher operation, fundamentals of lighting, graphics,
	video control and video recording, and real-time video production.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">Transfer acceptability: CSU</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
