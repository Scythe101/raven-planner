</p>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li><strong>Pre-Production:</strong> Preliminary project planning</li>
	<li><strong>Production:</strong> Filming in the classroom studio and in the field</li>
	<li><strong>Post-Production:</strong> Editing using classroom provided software</li>
	<li><strong>Professionalism:</strong> Responsibility and meeting course expectations</li>
</ul>
