<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed to give students the opportunity to refine their own artwork, called a "Body of Work." Seminar in Art is for students who would like to take a focused independent study format in art. Students will first explore a variety of media, and then decide on a focus to pursue. Drawing, painting, digital, 3D, and/or mixed media can be chosen as a means to create their "Body of Work." The importance of art in our daily lives and its career potential is highlighted through outside contacts such as college visitations, guest artists, gallery/museum visits, and studio visits. This class is helpful for students interested in taking AP 2D/AP Drawing or any upperclassman who has had at least 2 courses in visual art at CCA. <b>NOTE:</b> Seminar in Art does not provide AP credit or weight and those wanting to work digitally should have their own laptop with programs.
</p>
