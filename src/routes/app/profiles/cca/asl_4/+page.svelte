<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in ASL 3</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The successful student in this advanced course will be able to shift roles, maintain spatial
	agreement, properly sequence classifiers, react and elaborate appropriately with stories that
	include descriptions, sharing thoughts and reasoning. This class is in alignment with national
	standards at the intermediate-high level.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Typical activities include story telling and narratives; conversational practice; classifier analysis;
	fundamental voice and expressive interpreting; and analysis of ASL/ Deaf literature.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Outside of class, the students will familiarize themselves with Deaf culture, attend Deaf events and
	practice narratives that incorporate new vocabulary and grammar. In general, the successful
	student will spend approximately 30 minutes on work outside of class to prepare for the next class
	session.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Assessments include receptive and expressive presentations, dialogues, essays, projects, quizzes
	and tests.
</p>
