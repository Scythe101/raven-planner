<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Prior to taking the advanced recording arts class, it is highly recommended to have successfully completed the CTE Digital Composition and CTE Recording Arts courses. This course will build on the skills learned in these courses and offer lectures and projects based on more advanced audio topics. The Advanced Recording Arts course will cover professional level concepts including: the history of the recording industry and technology; the use of various recording hardware; advanced mixing and editing concepts; advanced mastering, sound reinforcement, music production skills, music business skills; and careers in the recording field. Students have access to industry software (Pro Tools, Reason, Sibelius) as well as hardware and microphones needed to complete professional grade recordings. Students will work on largely self-directed recording and audio production projects designed to mirror professional work in this field.
</p>

