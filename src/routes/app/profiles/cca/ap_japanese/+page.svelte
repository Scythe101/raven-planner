<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The AP Japanese Language & Culture course is designed to be comparable to college/university
	Japanese courses in which students complete approximately 300 hours of college-level classroom
	instruction. The goal of the AP Japanese Language & Culture course is to help students improve
	their writing and oral skills, listening comprehension, grammar use, and depth of vocabulary.
	Students will explore concepts of cultural significance. Students will develop the skills
	necessary to analyze a variety of written texts and communicate their ideas through discussion.
	Conversational skills are practiced daily while discussing assigned readings and in general
	classroom interactions. Students need strong study skills to meet the demands of this program.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This class is in alignment with national standards at the intermediate level.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Typical activities include conversation, essay writing and typing in Japanese.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	In general, the successful student will spend approximately 30 minutes on work outside of class on
	homework and review of notes and book content.
</p>

<h2 class="mt-8">Grading</h2>
<p class="text-xl 2xl:w-3/4">
	Assessments include oral presentations, dialogues, essays, projects, quizzes, tests, listening
	exercises and an interactive notebook.
</p>
