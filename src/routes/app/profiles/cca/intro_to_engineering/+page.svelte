<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Introduction to Engineering Techniques provides students with tools to pursue further study in engineering. This includes but is not limited to the study of strengths of materials, electronic circuits, mathematical analysis, and engineering design. Coursework is heavily project based learning as well as reading texts and writing. Assignments will be from various fields of engineering including Mechanical, Civil, Electrical, and Aerospace. The class is intended for those interested in exploring engineering as a career or seeking a CTE class that is connected to STEM. This course is UC Approved. <br><i>This course was previously called Principles of Engineering.</i>
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	A variety of assessments will be used including journal entries, projects and self and peer assessments. Example assessments include journal entries calculating the force on a bridge truss, demonstration of a project implementing a mechanical, electrical, and control mechanism to sort marbles, self-evaluation of a material strength project, and peer review/voting of simple machine energy transfer projects.
</p>

