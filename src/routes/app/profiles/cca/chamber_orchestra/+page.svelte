<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">
	Audition Only<br>
	Plays a STRING instrument (violin, viola, cello, bass, harp, and possibly piano)<br>
	Recommended to take the class both terms
</p>

<h2 class="mt-8">Course Description</h2>
<ul class="mr-4 mb-4 text-xl 2xl:w-3/4 list-disc ml-6 font-noto-serif">
	<li>Play standard orchestral literature in correlation to term content. There will be a strong emphasis on chamber orchestra playing within the Chamber Orchestra section. Students will also perform with the other Orchestra class regularly on more large scale works.</li>
	<li>Numerous concerts on and off campus during and outside of class time throughout the term in large and small ensembles, occasionally in full orchestra.</li>
	<li>Collaborations with Orchestra and other subject areas.</li>
	<li>Various comprehensive projects of term content.</li>
	<li>Advanced Theory (review, cadences, forms, suspensions, chord progressions, and modes).</li>
	<li>History (Fall: Medieval to Baroque; Spring: Classical to 20th Century and World composers, styles, etc.).</li>
	<li>Performance (string pedagogy, all major/minor scales).</li>
	<li>Ensemble (rehearsal etiquette and techniques).</li>
</ul>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Orchestra and Chamber Orchestra can meet simultaneously and will accommodate advanced players with pull-out collaborations and applications of content.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	<strong>Performances:</strong> Students will perform on campus and in the community as a part of this class. These performances are generally on evenings and weekends. Students will be notified well in advance of these dates.
</p>

<h2 class="mt-8">Grading</h2>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>50% Classwork</li>
	<li>20% Practice Reports (3 hours per week required)</li>
	<li>20% Playing Tests</li>
	<li>10% Written Work / Music Theory</li>
</ul>
