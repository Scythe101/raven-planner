<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed to prepare students to become effective speakers, presenters, and/or debaters in public discourse. The class meets California State Standards in English Language for Speaking and Listening, as well as including California High School Speech Association and National Forensics League forms of public address.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The course is divided into two sections for the semester: speech & debate. During the first quarter, the focus is on speech. Students are taught skills in speaking--both prepared and impromptu speeches. Speech assignments may be modeled after the National Forensics competition categories (extemporaneous, original oratory/advocacy, dramatic/humorous/duo/oratorical/thematic interpretation, original prose/poetry, expository, impromptu, student congress). Students learn how to speak effectively for given audiences. Students learn techniques that are effective in getting their point across, while avoiding common speaking mistakes. Students will learn to prepare and memorize speeches in a set time period. (Example: a two minute speech on a given topic.)
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The second quarter focuses on debate. Students will learn effective argument and debate skills. They will learn techniques in researching, organizing, and presenting information in an effective argument. Debate assignments may be modeled after the National Forensics competition categories (Lincoln Douglas, Policy, Parliamentary, Public Forum). Students wishing to compete on the CCA Speech and Debate Team may be allowed an alternate assessment schedule. This is at the teacherâ€™s discretion.
</p>
