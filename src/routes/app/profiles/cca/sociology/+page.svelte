<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Sociology is an academic social science elective course that is on the University of California's
	"a-g" college prep list, as well as being recognized as such by many other colleges and
	universities. To count for college entrance, a grade of "C" or higher is generally required.
	Sociology is the study of social groups, institutional structures, and social interaction.
	Sociology crosscuts cultural differences; all people sustain beliefs and values, conform in
	stylized relations, and construct institutions to support their culture. A central feature of the
	curriculum is a survey of the theories of prominent sociologists. These theories will be used to
	analyze some of the main sociological topics, as well as to help students formulate their own
	theories.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	The main topics will be chosen from the following, though others may be added as the course
	develops:
</p>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Social Interaction – Stereotyping, Race, Gender, Age, Sex/Dating</li>
	<li>
		Culture – Geography, Economics, Education, Language, Children, Values & Beliefs, Charity,
		Religion
	</li>
	<li>
		Social Organization – Institutions (i.e, Government, Family, Religion, Education), Social
		Classes
	</li>
	<li>Social Dynamics – Media, the 1960s, 1960s vs. Present, Role Models</li>
</ul>

