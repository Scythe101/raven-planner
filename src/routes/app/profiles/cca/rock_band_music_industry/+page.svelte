<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed to help students develop both the business skills and the music/performance skills needed to be successful as a professional in the music industry. Students will learn the essential skills needed to work in a number of sectors of the industry including booking, contracts, publishing and copyright laws, promotions, songwriting, recording and performance skills. They will also receive instruction in music theory, performance skills, and proper rehearsal techniques. At the conclusion of the course, students will turn in a portfolio of work from these areas of study that can be used for placement in internships and employment in the music field.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This is a CTE enrichment course, therefore, course standards and expectations are career and industry oriented. Students are expected to be self-motivated and independent, meet deadlines, and work efficiently and effectively in small groups.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	A variety of assessments will be used: live performance workshops and critiques, music theory quizzes, individual playing tests on various scales and musical concepts, music industry projects designed to mirror real world scenarios and skills, written work, and actual live performance opportunities.
</p>

<h2 class="mt-8">Class Setting</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students are expected to work both in group settings and independently. They will participate in group rehearsals that require strong communication skills and management of group dynamics. In addition to their performing groups, students will also work both in groups and independently to complete projects similar to those required in the music industry.
</p>

