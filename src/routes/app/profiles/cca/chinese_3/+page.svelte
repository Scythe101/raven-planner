<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in Chinese 2</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Chinese 3 is a rigorous academic course that takes an integrated communicative approach to
	empower students to speak, listen and read Chinese, and to comprehend diverse medium and texts
	on personal topics and everyday social interactions, culture, and Chinese society, and to
	write/type Chinese in presentational, and interpersonal modes.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will be able to listen, speak, read and write/type Chinese covering exchanges on
	everyday life including school life, home and family, shopping, friendship, dining, sports, living
	spaces, travel, and Chinese culture; comprehend the main points and some details of standard
	discourse and authentic material in Mandarin spoken at an intermediate low-level speed; to
	write/type about familiar topics and experiences in a paragraph with beginning, middle, and end;
	build proficiency in reading and writing/typing Chinese characters in simplified and/or
	traditional forms.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Traditional/Simplified Characters:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Chinese characters may either be written/typed in the simplified (简体) or the traditional system
	(繁體). The simplified system is used in Mainland China, while the traditional style is used in
	Hong Kong, Taiwan, and overseas Chinatowns. For Chinese 3, students can choose to write either
	traditional or simplified Chinese characters. Textbooks will be printed in both simplified and
	traditional Chinese and the character workbooks are in both traditional and simplified Chinese.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Whenever writing characters, the teacher will write both sets of characters in all examples, text
	materials, and handouts.
</p>
