<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students taking this course can expect to engage in an educational environment that is equivalent
	to a first-year university level survey course. AP Psychology is designed to introduce students to
	the systematic and scientific study of the behavior and mental processes of human beings and other
	animals. Students are exposed to the psychological facts, principles, and phenomena associated with
	the major subfields within psychology. The course stresses critical thinking, reading, and writing
	within the context of scientific methodology and questioning. AP Psychology follows the standards
	prescribed by the College Board in the AP Course Description (which can be found online at
	https://apcentral.collegeboard.org/pdf/ap-psychology-course-and-exam-description-0.pdf?course=ap-psychology)
	Additionally, students may wish to take the AP Psychology exam, for which they may earn college
	creditâ€”depending on the policies of colleges and universities. Students will complete readings from
	college-level textbooks and will complete written work commensurate with university-level classes.
	Additionally, students will take part in either group or individual research projects.
</p>

