<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Dance Conservatory is an audition-based, after-school extension of the daytime Envision program, offering smaller class sizes and more individualized instruction for the most advanced dance students.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Dance Conservatory aims to foster excellence in dance and the development of original thought and creativity, as well as encourage socially aware artists. Students in the Dance Conservatory will create new works that explore issues and common themes. They are encouraged to become thinking dancers.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The collaborative nature of the arts ensures that Conservatory students enjoy many opportunities to work with and learn from one another. A focus on interdisciplinary collaboration is best witnessed during the three-week, Conservatory-wide class called the Projects. The Projects put all Conservatory students on interdisciplinary teams of twelve to fifteen artists. Teams are facilitated by a CCA/Envision teacher, and each year a new theme is selected to unite all the Projects. At the end of the collaborative process, each team presents their Project to a live audience comprised of CCA students, staff and community members.
</p>

