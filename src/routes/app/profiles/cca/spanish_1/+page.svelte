<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Focus:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Spanish 1 places an emphasis on learning correct pronunciation, listening comprehension, vocabulary development, fundamentals of verb conjugations and the formation of simple, complete and grammatically correct Spanish sentences.
</p>

<h2 class="mt-8">Grading</h2>
<h3 class="font-dm-serif-display mt-4 text-2xl italic">Workload:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will keep a well-organized notebook, take notes, listen to language CDs/DVDs, work in groups and make oral presentations in Spanish.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Outside of class, the student will review notes, practice vocabulary and thoughtfully complete written assignments. In general, the successful student will spend approximately 15-30 minutes on work outside of class to prepare for the next class session.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Assessment:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
