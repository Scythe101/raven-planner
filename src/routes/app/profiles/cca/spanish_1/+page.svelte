<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The successful student in this introductory course will enter with a desire to learn the language and cultures of Spanish-speaking countries; a positive attitude and a willingness to challenge themselves to actively participate by asking questions, speaking, reading, writing and listening at a beginning level in Spanish. Students entering Spanish 1 will be expected to explore cultural differences without ridicule.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In the course, the successful student will be able to describe and narrate in the present tense to converse about his/her daily life, family, school and community. He/she will be able to read and write about related topics and demonstrate awareness of Spanish-speaking cultures. This class is in alignment with national standards at the novice level.
</p>

