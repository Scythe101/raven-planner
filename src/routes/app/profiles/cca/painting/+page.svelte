<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed to build on fundamental skills learned in Drawing & Design and introduces students to the world of painting through a variety of different mediums, including watercolor, gouache, and acrylic. Students will have the opportunity to explore different approaches, styles, surfaces, materials, tools, and techniques while further developing their understanding and application of 2-Dimensional design issues in their work. We will review basic photographic composition and students will be encouraged to take their own photos to use as reference for their paintings. Additionally, we will look at influential contemporary and historical artists for inspiration and to better understand the context of artmaking throughout history.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This is a studio based course meaning that most work will be completed during class time and will be project based. This is not a homework intensive course, but requires good attendance, a strong work ethic, and willingness to try new things every day. Homework might consist of sketching, finishing projects, and/or collecting reference images and taking photos. Grading for Painting classes typically consists of projects, studio performance, and classwork. Specific weighting of assignments is determined by each individual instructor.
</p>
