<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Spanish III course furthers the study of grammar, vocabulary and cultures of Spanish-speaking
	countries. Students improve listening, speaking, reading and writing skills. Students further
	develop reading comprehension skills through different types of text, oral presentations and
	written exercises.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Beliefs:</h3>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>All students can learn and experience success in a world language.</li>
	<li>
		Second language acquisition provides the vision and skills necessary to be a global citizen.
	</li>
	<li>A primary goal of world language education is real-life communication.</li>
	<li>World language education develops critical thinking skills.</li>
	<li>
		Assessment must reflect the goals and learning targets set forth in this curriculum document.
	</li>
	<li>World languages must be part of the core curriculum in 21st Century schools.</li>
	<li>
		Students will read authentic texts, mainly of an expository nature, and apply various reading
		comprehension strategies.
	</li>
</ul>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">
	What Students Should Know About the Class:
</h3>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>The course is taught entirely in Spanish.</li>
	<li>Tests are not multiple-choice; they are open-ended and will contain listening portions.</li>
	<li>Students are expected to speak Spanish in class 100% of the time.</li>
	<li>There are end-of-unit speaking tests that are worth 40% of your grade.</li>
	<li>
		There is a test every week. Each test includes grammar, vocabulary, a listening activity, etc.
	</li>
	<li>
		You are expected to know the difference between the preterit and imperfect tenses when you enter
		in SP III.
	</li>
	<li>You are expected to be able to speak and form coherent sentences.</li>
	<li>
		The classes move at a more rapid pace than in middle school. You WILL need to study every day.
	</li>
	<li>The class is difficult. Be on task in class and review your notes every day.</li>
	<li>
		Use the World Language Help Desk if you feel like you are falling behind or you feel lost.
	</li>
	<li>You will learn A LOT of new verb tenses and grammar concepts in Spanish 3.</li>
	<li>
		Teachers will use different online teaching platforms that focus on interactivity and
		collaboration between teachers and students.
	</li>
</ul>

