<h2 class="mt-8">Student Background</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students entering Integrated Math 2 should already have a good understanding of the
following concepts:
</p>
<ul class="font-noto-serif mr-4 ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Working with radicals and integer exponents</li>
	<li>Understanding the connection between proportional relationships, lines, and linear equations.</li>
	<li>Solving linear equations as well as apply graphical and algebraic methods to analyze and solve systems of linear equations in two variables.</li>
	<li>Understanding arithmetic and geometric sequences and their relationship to linear and exponential functions.</li>
	<li>Defining, evaluating, and comparing functions, and use them to model relationships among quantities.</li>
	<li>Understanding congruence and similarity using transformational geometry.</li>
	<li>Solving real-world and mathematical problems using linear and exponential mathematical models.</li>
</ul>
<h2 class="mt-8">Course Content and Expectations</h2>
<p class=" mr-4 text-xl 2xl:w-3/4">
	In Integrated Math 2, students will learn concepts such as:
</p>

<ul class="font-noto-serif mr-4 ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Manipulating algebraic expressions, including rearranging, collecting terms, factoring, applying properties of exponents, and transforming expression between different forms.</li>
	<li>Understanding properties of quadratic expressions and functions equations (including roots, vertices, intervals of increasing/decreasing).</li>
	<li>Understanding the concept of a function, domain, and range, and identifying properties of functions and graphs.</li>
	<li>Interpreting functions given graphically, numerically, symbolically, and verbally.</li>
	<li>Modeling with functions using tables, functions, and understanding when the context allows for a model that is only an approximation.</li>
	<li>Constructing and comparing and linear, exponential, and quadratic models to solve real-life problems.</li>
	<li>Writing, interpreting, and translating among various forms of quadratic equations.</li>
	<li>Graphing and Analyzing properties of absolute-value functions and piece-wise functions.</li>
	<li>Experimenting, conjecturing and proving properties of triangles, quadrilaterals, polygons and circles.</li>
	<li>Using similarity to define and solve problems using right-triangle trigonometry.</li>
	<li>Using a coordinate system to analyze properties of circles and parabolas.</li>
	<li>Finding probability of independent, dependent and conditional events by experimentation, theoretical model, Venn diagrams, tree diagrams, and two-way tables.</li>
</ul>
