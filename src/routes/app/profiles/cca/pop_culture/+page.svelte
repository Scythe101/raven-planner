<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course provides an introduction to critical issues and approaches in the study of 20th
	century popular culture. Popular culture is all around us, influencing how we think, how we feel,
	how we live our lives in countless ways. In this course, we will examine how history influences
	the popular culture of the times. We will examine popular culture decade by decade, starting with
	the 1900s and taking time to address popular culture in the 2000s. We will learn how to critically
	approach the study of popular culture through readings, discussions, assignments, and
	presentations of material to others. This course covers 20th century American popular culture from
	the 1900s through today.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	This course is meant to be interactive and engaging, however, it will also be academically
	challenging. In this course, we will examine the popular culture of each of the ten decades of the
	20th century (1900 – 1909, 1910 – 1919, 1920 – 1929, 1930 – 1939, 1940 – 1949, 1950 – 1959, 1960 –
	1969, 1970 – 1979, 1980 – 1989, 1990 – 1999, and the 2000s). We will explore the historical events
	of the decade upon which the popular culture of the decade was based. We will then explore the
	specific details of the popular culture of the decade, which include film, food, fashion, fads,
	sports, television, and music of the decade.
</p>

<h2 class="mt-8">Grading</h2>
<p class="text-xl">
	Grades will be based on a mix of brief quizzes, individual and group presentations, short research
	papers on topics that are chosen by the student, and group projects.
</p>
