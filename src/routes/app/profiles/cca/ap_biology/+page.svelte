<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The AP Biology focuses on enduring, conceptual understandings and the content that supports them.
	Students spend less time on factual recall and more time on inquiry-based learning of essential
	concepts, helping them develop the reasoning skills necessary to engage in the science practices
	used throughout their study of the 4 Big Ideas in AP Biology. The big ideas serve as the
	foundation of the course and allow students to create meaningful connections among course
	concepts.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	The AP Biology class encourages student development of inquiry and reasoning skills, such as
	designing a plan for collecting data, analyzing data, applying mathematical routines, and
	justifying arguments using evidence. The result will be readiness for the study of advanced topics
	in subsequent college courses.
</p>

