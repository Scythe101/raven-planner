<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Advanced Fine Art Digital Photography is designed for students who loved their experience in the
	beginning Fine Art Digital Photography and PHOTO Imaging courses and would like to challenge
	themselves with a more advanced, self-directed study in the art of digital photography. Students
	who have nothing to "say" artistically will have a difficult time in this class. In this advanced
	class, students will be concentrating on producing a "body of photography work" that represents
	their own artistic vision. Students will be required to work every week creating, improving, and
	modifying their body of work.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will also be expected to advance their knowledge of the technical aspects of photography
	with the goal that the camera will eventually become such a natural tool for them that they can
	concentrate wholly on the art making process of photography. Students, with instructor approval,
	select their own "Body of Work" assignments that take place over several weeks with weekly checks
	on progress. Additional "Pic-A-Day" assignments allow students constant practice and challenges so
	that students can constantly push themselves.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will be required to visit one visual arts museum, gallery, or opening per quarter of
	class to further their involvement and inspiration from other artists.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will have a minimum of 1-2 hours per week of shooting time homework.
</p>

<h2 class="mt-8">Grading</h2>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>
		30% Practice Work (Picture a day, sketchbook, research, in-class practice exercises, etc.)
	</li>
	<li>70% Presentation Work (major project checks and final Body of Work Images)</li>
</ul>
