<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students taking this course can expect to engage in an educational environment that is equivalent
	to a first-year university level survey course. AP Economics actually consists of two components:
	Macroeconomics and Microeconomics and are considered courses that emphasize the general principles
	of each field. The College Board offers two separate exams for each discipline, and grades are
	reported as two separate grades in this respect. The student can choose to take one exam or the
	other, or both. When studying Microeconomics, students examine the principles of economics as they
	apply to individuals in a society from the perspective of both producers and consumers. The role
	of markets in the economy is studied, with emphasis on the ways in which the government interacts
	with the economy. Conversely, the study of Macroeconomics discusses the economy at large. Topics
	such as the concept of scarcity and opportunity cost are applied in this aspect of the course.
	When studying Macroeconomics, students also apply the concepts of supply and demand to the larger
	economic system, as well as the general workings of the free market economy. The AP Economics
	course follows the standards prescribed by the College Board in the AP Course Description (which
