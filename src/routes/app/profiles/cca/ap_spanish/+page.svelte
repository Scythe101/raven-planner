<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C or better in Spanish 3, 4, 5, Spanish for Spanish Speakers</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The AP Spanish Language and Culture course emphasizes communication (understanding and being
	understood by others) by applying interpersonal, interpretive, and presentational skills in
	real-life situations. This includes vocabulary usage, language control, communication strategies,
	and cultural awareness. The AP Spanish Language and Culture course strives not to overemphasize
	grammatical accuracy at the expense of communication. To best facilitate the study of language and
	culture, the course is taught almost exclusively in Spanish.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The AP Spanish Language and Culture course engages students in an exploration of culture in both
	contemporary and historical contexts. The course develops students' awareness and appreciation of
	cultural products (e.g., tools, books, music, laws, conventions, institutions); practices
	(patterns of social interactions within a culture); and perspectives (values, attitudes, and
	assumptions).
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Description of an AP Language Student:</h3>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>
		The student has strong communicative ability in Spanish in interpersonal, presentational, and
		interpretative modes. Students are typically in their fourth year of high school–level Spanish
		language study. In the case of native or heritage speakers, there may be a different course of
		study leading to this course.
	</li>
	<li>
		The student has a strong command of Spanish linguistic skills that supports his ability to
		communicate.
	</li>
	<li>The student comprehends Spanish intended for native speakers in a variety of settings.</li>
	<li>The student is able to comprehend information from authentic sources in Spanish.</li>
	<li>The student is aware of some cultural perspectives of Spanish-speaking people.</li>
</ul>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Workload:</h3>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Daily Journal writings in response to prompts or readings</li>
	<li>
		Oral and written reports of TV News and current events from a TV Newscast or radio news program,
		in Spanish
	</li>
	<li>
		Reading from a wide variety of sources including newspapers, magazines, internet articles, short
		stories, essays, and poetry
	</li>
	<li>Formal and Informal writing such as essays, letters, post-cards, e-mail</li>
	<li>
		Oral presentations on a variety of topics, including cultural themes, interviews, skits, and
		dialogues
	</li>
	<li>Listening to narratives, dialogues, interviews and other authentic sources</li>
	<li>Review and practice of grammatical structures in context</li>
	<li>
		Participation – coherent conversation in Spanish through in-class discussions and pair/group
		activities
	</li>
	<li>AP Exam format timed writing</li>
	<li>AP Practice Exams</li>
</ul>

