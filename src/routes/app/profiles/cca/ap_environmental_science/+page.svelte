<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	AP Environmental Science provides students with the scientific principles, concepts, and
	methodologies required to understand the interrelationships of the natural world, to identify and
	analyze environmental problems, both natural and human-made, to evaluate the relative risks
	associated with these problems, and to examine alternative solutions for resolving and/or
	preventing them.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Environmental Science is interdisciplinary; it embraces a wide variety of topics from different
	areas of study (e.g. biology, chemistry, earth science, geography), yet there are several major
	unifying themes that cut across the many topics included in the study of environmental science.
</p>

