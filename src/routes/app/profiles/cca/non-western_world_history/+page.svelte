<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is highly recommended for 9th grade students who are planning to take AP World History
	in 10th grade. While it is not an Advanced Placement (AP) course, it is an excellent opportunity
	to take a Social Science course in the 9th grade to continue to develop skills learned in middle
	School and to prepare skills required for AP. The course is open to all students regardless of
	whether they will take or have taken AP World History. However, for those students who have
	completed AP World History, this course provides a helpful, in-depth study of topics that are
	relevant to the AP exam in World History and will allow students a means of extending their
	learning.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Additionally, completion of this course would allow the students the ability to apply the
	knowledge gained in this course on the SAT II World History exam. The CP World History standards
	in California do not meet the content demands of the SAT II exam; this course can help increase
	those scores.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course seeks to study the last fifty years of world history from the Non-Western perspective.
	The purpose of the course will be to study the contemporary issues facing the world today that
	derive their origin in these regions. It will also serve as an introduction to Non-Western world
	geography and cultures. While current events in Non-Western regions will often drive in-class
	discussions, the point of the course is to determine the causes of these current events looking
	back over the last fifty years. Along with a geographic introduction, the course will study the
	cultural and religious foundations of Non-Western regions. This course is designed as a part of a
	comprehensive Social Science program and specifically complements most core academic requirements
	(specifically: College Preparatory World History, AP World History, Sociology, Economics, and
	Current Issues.)
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course extends the study of World History (both AP and College Prep) with a more in-depth
	exploration of history that includes and exceeds current standards established by the State.
	Course work will include research projects, with at least two multimedia presentations or
	simulations. Students may also analyze primary-source historical documents and complete essays
	based on documents of these sorts, often referred to as a Document Based Essay.
</p>

