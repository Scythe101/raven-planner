<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This capstone course in the Engineering Technology CTE pathway at CCA provides students with advanced, hands-on experience in digital electronics. Students will engage in project-based learning aligned with engineering and architecture standards, including the design and implementation of combinatorial logic, sequential timers, and more. The course emphasizes independent and group work, problem solving, and project-based learning. Industry professionals review the course annually to ensure relevance. Students will showcase projects to local businesses and community members each February.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	A variety of assessments will be used including journal entries, projects, and self and peer assessments. Example assessments include warm-up exercises to the 3dB point on a filter, demonstration of combinatorial logic projects, self-evaluation of sequential timer design, and peer review/voting of final class projects.
</p>

