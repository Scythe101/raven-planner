<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	English 10 College Prep is a one-term course organized around the study of world literature with
	the theme of "Journey into the World." It includes short stories, expository texts, novels, drama
	and poetry. Students will be challenged and given the opportunity to grow in their practical
	language skills through meaningful class discussions, journal prompts, essay writing, creative
	projects, oral presentations, and theater arts. Students will continue to use and practice
	grammar, punctuation, and vocabulary skills. With an understanding of the fundamentals of research
	elements, students will construct multi-paragraph essays that respond to expository texts, analyze
	literature, persuade, and narrate events, as well as experiment with other written forms, such as
	business letters and technical documents. The writing focus is designed to prepare students for
	the SAT. CAHSEE and college level writing requirements. Students will develop critical thinking
	skills based on teacher modeling and will apply them to assignments independently and in
	cooperative, learning groups.
</p>

