<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">Plays a traditional big band instrument (saxes, trumpets, trombones, drumset, piano, guitar, bass). Non-traditional jazz instruments will be considered on a case-by-case basis at the audition.<br>Recommended to take year long/both terms.</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed to provide the opportunity for in-depth study in the area of jazz. This will be accomplished through research, written and oral presentations, and performance. Through this study students will thoroughly understand the cultural and artistic relevance of particular jazz compositions throughout history, and the role of the jazz musician in American culture. Students may gain experience in Big Band and Jazz Combo settings, depending on instrumentation and ability.
</p>
<ul class="mr-4 mb-4 text-xl 2xl:w-3/4 list-disc ml-6">
	<li>Musical concepts: tone, intonation, improvisation, individual technique, ensemble techniques</li>
	<li>Performances: Students will perform on campus and in the community as a part of this class. These performances are generally on evenings and weekends. Students will be notified well in advance of these dates.</li>
</ul>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	<b>Supplemental Information:</b> This is a very advanced musical group, and will work at a fast pace with very high standards of performance, playing advanced high school and college level literature. This class builds on the Symphonic Band and Rock Band curricula.
</p>

<h2 class="mt-8">Grading</h2>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Classwork: 50%</li>
	<li>Practice reports (3 hours per week required): 20%</li>
	<li>Playing tests: 20%</li>
	<li>Written work/music theory: 10%</li>
</ul>
