<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in Japanese 1</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The student should enter with a solid foundation in all skills described for Japanese 1. For
	example, the successful student will be able to introduce himself/herself (name, age, birthday,
	likes/dislikes, daily activities, family), answer questions orally and write basic sentences using
	hiragana, katakana and kanji.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The successful student in this continuing course will be able to converse about his/her life in
	greater detail than in Japanese 1, including family origin, getting sick, food, fashion and weather.
	Speaking and writing will reflect appropriate cultural knowledge, skills and awareness. Grammar
	instruction increases as the student creates more original expressions; topics include the plain
	form of verbs, adjective inflections and particles. Students will also be able to express their opinion
	at the basic level using "I think..."
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In addition to all "hiragana" characters and "katakana" characters, the student will be able to read,
	write and type approximately 150 "kanji" characters. This class is in alignment with national
	standards at the novice-high level.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Activities:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Typical activities include a role play inviting a friend to go out, producing a weather forecast,
	creating Kanji character art via calligraphy, performing a fashion show, and observing a traditional
	Japanese festival.
</p>

<h2 class="mt-8">Grading</h2>
<h3 class="font-dm-serif-display mt-4 text-2xl italic">Workload:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In class, the student will maintain a well-organized notebook, take notes, listen to language CDs,
	work in groups and role-play situations.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Outside of class, the student will review notes, practice vocabulary, drill character flashcards and
	thoughtfully complete written assignments. In general, the successful student will spend
	approximately 30 minutes on work outside of class to prepare for the next class session.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Assessment:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Assessments include class participation, homework, oral presentations, essays, projects, quizzes
	and tests.
</p>
