<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	College Prep Physics: Of the Universe is a year-long introductory physics course aligned with the
	California Next Generation Science Standards. In this course, students will explore physical laws
	that govern everything, including motion and forces, energy, electricity and magnetism, earth and
	space science, and engineering practices. Throughout the course, students will engage in
	discussion, hands-on activities, and experiments, observe real-world phenomena, and ultimately
	form conclusions based on evidence. Physics, earth science, and engineering topics are integrated
	throughout the course to give students even more real-world relevance and understanding.
</p>

