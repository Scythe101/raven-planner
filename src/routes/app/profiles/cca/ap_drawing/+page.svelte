<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In order to be successful in this class, you need to have taken Drawing and Design, and upper level courses such as Painting, Seminar, Advanced Drawing, and AP 2-Dimensional Design. The AP Drawing course addresses sophisticated and subtle aspects of drawing that go way beyond the fundamentals, so the teacher will present the material assuming you have already taken the prerequisite courses. This is the ideal course for the advanced art student who is eager to produce high-level work.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	“The Drawing Portfolio is designed to address a very broad interpretation of drawing issues and media. Light and shade, line quality, rendering of form, composition, surface manipulation, and illusion of depth are drawing issues that can be addressed through a variety of means, which could include painting, printmaking, mixed media, etc. Abstract, observational, and inventive works may demonstrate drawing competence. The range of marks used to make drawings, the arrangement of those marks, and the materials used to make the marks are endless.” – The College Board AP
</p>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Explore a breadth of high caliber work that includes examples of unity/variety, balance, emphasis, contrast, rhythm, repetition, proportion/scale, and figure/ground relationship</li>
	<li>Develop a personal concentration that is an in-depth investigation of a visual idea in concept, composition, and execution (15 pieces total)</li>
	<li>Select top quality pieces for presentation that successfully demonstrate mastery of drawing issues and are no larger than 18” x 24” (5 pieces total)</li>
</ul>

