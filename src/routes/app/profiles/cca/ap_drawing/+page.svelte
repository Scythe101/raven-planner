<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">
	The ability and background to complete college-level work.<br>
	Drawing and Design, 2-3 upper level art courses (Painting, Advanced Drawing, AP 2-Dimensional Design, Seminar in Art, Digital Art and Design, Digital Photo).
