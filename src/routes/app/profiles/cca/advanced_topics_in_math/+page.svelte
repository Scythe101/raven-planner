</ul>
<br />
<p class="mr-4 text-xl 2xl:w-3/4">
	This course is not intended to be taken in lieu of Intro to Calculus, or AP Calculus. It is <strong
		>not recommended</strong
	> that a student enroll in Advanced Topics before completing AP Calculus. The intention of this course
	is to have a post-Calculus math course that allows a student to explore advanced math without significantly
	increasing out of class workload.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	A very significant portion of the grade is determined by a student’s active involvement in
	presenting to the class, research findings, daily participation, and an instructional unit to the
	class through a sequence of lessons. Typically, assignments will be completed in class and there
	will be minimal homework. Course assignments could include written response questions, essays,
	class presentations, computational and paragraph explanation of problems, and individual and group
	discourse evaluations. Students will perform peer evaluations to provide feedback to the class and
	are expected to be self-reflecting their work and content knowledge throughout the course.
</p>
