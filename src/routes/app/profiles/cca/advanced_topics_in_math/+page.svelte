<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	The purpose of this course is for students to gain an appreciation of skills and concepts in the
	high school mathematics curriculum, while providing them with a foundation for future math courses
	and other disciplines reliant on mathematics. This course will serve to develop students' problem
	solving and reasoning skills through the solving of university level undergraduate and graduate
	mathematics including Group Theory, Combinatorics, Number Theory, Euclidean/non- Euclidean
	Geometry, Complex Analysis, Probability, and more in individual and group settings. Additionally,
	students will refine their communication skills, expand the skills necessary to work in a
	collaborative setting, and provide a platform for students to draw connections among mathematical
	concepts and their community. Students will be responsible for individual research, synthesizing
	information, presenting findings, running lessons and appropriate critique of others’ work.
</p>
