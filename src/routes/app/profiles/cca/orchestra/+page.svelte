<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">Play a STRING instrument (violin, viola, cello, double bass, harp) ONLY.<br>This is NOT a beginning level class. If you are unsure about your level of preparation, please see Ms. Villanova for an advisory audition so she can recommend the best placement.<br>Recommended to take year long/both terms.</p>

<h2 class="mt-8">Course Description</h2>
<ul class="mr-4 mb-4 text-xl 2xl:w-3/4 list-disc ml-6">
	<li>Play standard orchestral literature in correlation to term content. Students will also perform with the Chamber Orchestra class regularly on more large scale works.</li>
	<li>Numerous concerts on and off campus during and outside of class time throughout the term in large and small ensembles, occasionally in full orchestra.</li>
	<li>Collaborations with Chamber Orchestra and other subject matters.</li>
	<li>Various comprehensive projects of term content.</li>
	<li>Advanced Theory (review, cadences, forms, suspensions, chord progressions, and modes).</li>
	<li>History (Fall: Medieval to Baroque, Spring: Classical to 20th Century and World) (composers, styles, etc.).</li>
	<li>Performance (string pedagogy, major/minor scales).</li>
	<li>Ensemble (rehearsal etiquette and techniques).</li>
	<li>Orchestra and Chamber Orchestra can meet simultaneously with pull-out collaborations and applications of content.</li>
	<li>Performances: Students will perform on campus and in the community as a part of this class. These performances are generally on evenings and weekends. Students will be notified well in advance of these dates.</li>
</ul>

<h2 class="mt-8">Grading</h2>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Classwork: 50%</li>
	<li>Practice reports (3 hours per week required): 20%</li>
	<li>Playing tests: 20%</li>
	<li>Written work/music theory: 10%</li>
</ul>
