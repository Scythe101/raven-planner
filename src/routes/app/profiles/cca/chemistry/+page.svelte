<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	College Prep Chemistry: In the Earth System is a year-long introductory course aligned with the
	California Next Generation Science Standards. In this course, students will explore matter, its
	properties, and its interactions with other matter and energy. Students will deepen their
	understanding of chemistry through earth and space science, materials science and environmental
	science phenomena. Patterns, relationships, reactions, and energy exchange are emphasized
	throughout the course.
</p>

