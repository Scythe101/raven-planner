<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Humanities Conservatory is an audition/application-based, after-school extension of the daytime courses in Language Arts, Social Sciences and Envision Departments. Humanities Conservatory offers smaller class sizes and more individual instruction for the most advanced students of the Humanities.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Conservatory Humanities aims to deepen the student's skills in scholarly research to be applied to the wider world, both within the school and outside community. Visiting professors, along with CCA/Envision teachers, will prepare the student to master university-level research and expository writing.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Conservatory Humanities students will take daytime humanities courses and then after school, will take courses in Philosophy, Theology, Civics and Ethics. All Humanities Conservatory students will apply for their projects to be published. The collaborative nature of the Humanities ensures that Conservatory students will enjoy many opportunities to learn and work with other disciplines within the framework of the Envision Conservatory program. A focus on interdisciplinary collaboration is best witnessed during the three-week, Conservatory-wide class called The Projects. The Projects put all Conservatory students on interdisciplinary teams of twelve to fifteen artists and scholars. Teams are facilitated by a CCA/Envision teacher, and each year a new theme is selected to unite all the projects. Examples of such themes include "Beginnings" and "Dreams." At the end of the three weeks, each team must present their Project (a publication, performance, exhibit, etc) to the entire CCA school community.
</p>

