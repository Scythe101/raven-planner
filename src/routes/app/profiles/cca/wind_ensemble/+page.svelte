<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">Plays a traditional woodwind, brass, or percussion instrument.<br>Audition only.<br>Recommended to take year long/both terms.</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed to provide the opportunity for in-depth study in the areas of solo, chamber music, and band literature as it applies to their instrument. This will be accomplished primarily through performance, and may include research, written and oral presentations. Students will understand the cultural and artistic relevance of particular compositions throughout history, and the role of the musician in a given societal context.
</p>
<ul class="mr-4 mb-4 text-xl 2xl:w-3/4 list-disc ml-6">
	<li>Musical concepts: tone, intonation, articulation/sticking, individual technique, ensemble techniques, musicality</li>
	<li>Performances: Students will perform on campus and in the community as a part of this class. These performances are generally on evenings and weekends. Students will be notified well in advance of these dates.</li>
	<li>Symphonic Band and Wind Ensemble may meet simultaneously and will accommodate advanced players with pull out collaborations.</li>
</ul>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	<b>Supplemental Information:</b> This is a very advanced wind/percussion group, and will work at a fast pace with very high standards of performance, playing advanced High School and College level literature. This class builds on the Symphonic Band curriculum.
</p>

<h2 class="mt-8">Grading</h2>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Classwork: 50%</li>
	<li>Practice reports (3 hours per week required): 20%</li>
	<li>Playing tests: 20%</li>
	<li>Written work/music theory: 10%</li>
</ul>
