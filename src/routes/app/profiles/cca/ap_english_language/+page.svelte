<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	The AP English Language and Composition course is a one term course which provides an opportunity
	for advanced high school students to pursue and receive credit for college-level course work.
	Accordingly, the rigors of the course are intended to be similar to that of introductory college-
	level rhetorical and composition courses. Students receive training to become critical thinkers,
	readers, analysts of language, effective writers, and creative, cogent producers of argument.
	Students focus on the writing skills needed to be successful in the course and in their later
	college work. Students write effectively for a range of audiences and a variety of purposes,
	demonstrate mastery of the conventions of standard written language, and use the steps of the
	writing process as needed. American Literature—including both fiction and non-fiction works—may be
	the vehicle used to study language and rhetoric. However, the majority of the reading will come
	from college readers of expository writing, as required by the College Board. An important goal of
	this course is to help prepare students to do well on the AP English Language and Composition
	exam, given in May. The course will educate students on the expectations of the AP examiners and
	will provide many opportunities to practice writing and reading in the exam format.
	<br />
	<br />
</p>
