<h3 class="mt-6 text-2xl">Workload Expectations for an AP Language Student</h3>
<p class="mr-4 text-xl 2xl:w-3/4">
	<strong>NOTE:</strong> Individual work depends on the length and involvement of the assignment, project,
	essay, group project, etc.
</p>
<ul class="mr-4 ml-6 text-xl 2xl:w-3/4">
	<li>
		1-2 major literary works (i.e. novels, autobiographies, plays, etc.) or units (i.e. short
		stories, essays, poetry, etc.) –being assigned 25-60 pages of reading per class period (for
		major works)
	</li>
	<li>10-15 analyses from College Reader/analysis of reading</li>
	<li>1-2 pages of written response to the literary works/units</li>
	<li>
		2 or more multi-draft essays -- research, process pieces, and in-class / take home drafts.
	</li>
	<li>5-10 expressive writings—allowing students to practice various styles/modes of writing</li>
	<li>
		15- 20 AP Style timed writings (including essay tests and practice for the DWA)—about 10 per
		quarter
	</li>
	<li>
		Extensive AP test preparation—including multiple choice techniques; strong emphasis on critical
		reading, thinking, and analysis
	</li>
	<li>1-2 projects (group or individual)</li>
</ul>
