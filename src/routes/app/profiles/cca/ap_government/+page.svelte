<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	Beginning in Fall 2018, the newly redesigned APÂ® U.S. Government and Politics Curriculum
	Framework combines disciplinary skills with a deep understanding of political concepts. In this
	way the course not only aligns with a one-semester introductory college course in U.S. government,
	but also prepares students for advanced political science coursework and active, informed
	participation in our constitutional democracy.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	AP United States Government and Politics is a college-level introduction to key political
	concepts, ideas, institutions, policies, interactions, roles, and behaviors that characterize the
	constitutional system and political culture of the United States. Students will read and analyze
	U.S. foundational documents, Supreme Court decisions, and other texts and visuals to gain an
	understanding of the relationships and interactions between political institutions and behavior.
	They will read and interpret data, develop evidence-based arguments, and engage in an applied
	civics or politics research-based project.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	The AP U.S. Government and Politics Curriculum Framework is composed of two sections: "Political
	Science Skills and Practices" and the "Content Outline."
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	The content outline is organized around five big ideas, which focus on major concepts in U.S.
	government and politics. Within each big idea, a number of enduring understandings (EUs) focus on
	specific aspects of the big idea and delineate the level of conceptual understanding required of
	successful AP students in this course.
</p>

