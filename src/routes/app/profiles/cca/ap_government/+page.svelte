<h3 class="font-dm-serif-display mt-4 text-2xl italic">
	Political Science Research or Applied Civics Project
</h3>
<p class="mr-4 text-xl 2xl:w-3/4">
	The required project adds a civic component to the course, engaging students in exploring how they
	can affect, and are affected by, government and politics throughout their lives. The project might
	have students collect data on a teacher-approved political science topic, participate in a
	community service activity, or observe and report on the policymaking process of a governing body.
	Students should plan a presentation that relates their experiences or findings to what they are
	learning in the course.
</p>

