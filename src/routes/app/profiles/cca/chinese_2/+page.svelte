<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in Chinese 1</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Chinese 2 is a rigorous academic course that takes an integrated communicative approach to
	empower students to speak Chinese (by continuing to master the Pinyin system, communicating
	through accurate pronunciation and intonation), to listen to Chinese (understand everyday
	narratives and dialogues and ideas), to read Chinese (comprehend texts on personal topics and
	everyday social interactions, culture, and Chinese society), and to write/type Chinese (in various
	forms – interpretive, and descriptive modes).
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The objective of the class is to speak in a way that a Mandarin speaker can understand and
	interpret your intent without difficulty in everyday survival situations on topics including school
	life, home and family, shopping, transportation, weather, dining, asking directions, birthday
	party, and transactions in a store or restaurant; comprehend the main points of standard
	discourse and authentic material in Mandarin spoken at a beginner level speed. Build
	proficiency in reading and writing/typing Chinese characters in simplified and traditional forms.
	Write simple descriptions and narrations of paragraph length on everyday events and situations
	in different time frames. Read with understanding authentic written material in Chinese on a
	variety of topics related to daily life in the target-language world.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Traditional/Simplified Characters:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Chinese characters currently may either be written in the simplified (简体) or the traditional
	system (繁體). The simplified system is used in Mainland China, while the traditional style is
	used in Hong Kong, Taiwan, and overseas Chinatowns. Students write either traditional or
	simplified Chinese characters. Textbooks will be printed in both simplified and traditional
	Chinese and the character workbooks are in both traditional and simplified Chinese.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The aim is for all students in the first and second years to learn, recognize and write both
	character sets. This is important from a number of perspectives. For instance, from a linguistic
	perspective, many sounds are linked to radicals and characters in both traditional and simplified
	scripts and also since our students come from a variety of backgrounds, we hope to encourage a
	variety of heritage and Chinese-character (like students who have taken Japanese) learners.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Finally, our curriculum follows the standards recommended by a majority of colleges and
	universities in the United States including the UC/CSU systems.
</p>
