<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course serves as the Concentrator course for CCA's Biotechnology CTE Pathway. The goal of
	this course is to provide students interested in Biotechnology with in-depth understanding of the
	subject, and to develop their abilities as collaborative, creative problem solvers. The knowledge
	and skills acquired in Biotechnology 1 lay the foundation for students to move on to Biotechnology
	2.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students will develop their understanding of biotechnology by studying the structure, function,
	and processes of living things; and of the fundamental concepts, principles, and practices of
	biological science and the interaction of biology, chemistry, physics, mathematics, engineering,
	and information. Students will conduct lab investigations in which they pose questions and define
	problems, plan and carry out experimental protocols, construct explanations and design solutions,
	and develop models to explain observed outcomes. Students will develop communication and
	collaboration skills, and explore career opportunities.
</p>

