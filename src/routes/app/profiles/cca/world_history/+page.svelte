<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	College Preparatory (CP) World History fulfills both state and district requirements for high
	school graduation. The course examines world history in the modern period, roughly 1450 to the
	present, including an analysis of the causes and events of the two World Wars. Instruction is
	centered not on merely memorizing the events of the world's history, but on teaching students to
	critically analyze history in order to discern human commonalities and better understand the human
	condition.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students at Canyon Crest Academy are introduced to the skill of historical document analysis, and
	a critical component of this course is the completion of a standards-based, cross-curricular
	research project based in the content of World History. Additionally, periodic study of current
	events, related to themes and strands in World History, are explored.
</p>

