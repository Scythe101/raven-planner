<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	The U.S. government course introduces students to the requirements and demands of democratic
	citizenship. This course will develop informed, responsible participation in American political
	life by competent citizens committed to the fundamental values and principles of American
	constitutional democracy. This course studies the nature and function of political relationships:
	citizen-to-citizen, citizen to local community, citizen to state, and citizen to the nation. It
	provides students with the foundation essential to understanding the "why" of governmental policy
	and action. By building a solid foundation for understanding the dynamics of American politics,
	the course will also stimulate within the student a sense of political efficacy and activism on
	issues of personal interest. In short, U.S. Government will act as one of the two "finishing
	courses" of the Social Sciences, preparing students to take their rightful place as full, active,
	and responsible members of American society while simultaneously integrating in them an
	appreciation of the past as it relates to the present and preparing them for the possibilities of
	their future.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	This course is aligned to California academic standards for U.S. Government. There will be four
	units:
</p>

