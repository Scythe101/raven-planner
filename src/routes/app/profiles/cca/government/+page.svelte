	<li>Civil Liberties and Civil Rights</li>
	<li>American Political Ideologies and Beliefs</li>
</ul>

<p class="mr-4 text-xl 2xl:w-3/4">
	Typical assignments in this course may include assessment of current events by watching television
	news programs and events (such as political debates), visiting news organization websites, or
	reading the newspaper. Additionally, students may be expected to complete independent projects as
	a requirement of the course. Examples of this might include the researching and writing of a bill
	to be presented before a mock legislature, preparation for a debate over a controversial law or
	court case, analysis of Supreme Court cases, or the writing of editorials and letters to the
	editor.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
