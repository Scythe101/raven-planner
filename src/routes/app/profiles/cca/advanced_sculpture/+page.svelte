<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students in Advanced Sculpture will build on the knowledge learned in Sculpture. This course is
	project-based and success depends on effort demonstrated by the student to commit to the creative
	process and the quality of their artwork. Advanced Sculpture is designed to continue an in-depth,
	intensive study of three-dimensional arts, while practicing and mastering techniques and concepts
	previously learned. Students are expected to apply higher level skills to clay and other mixed
	media. Students will be introduced to new concepts and learn to problem solve artistic outcomes.
	Students may be required to give peer assessments, presentations, and critiques throughout the
	course.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students are graded in two main areas: <strong>Projects</strong> and
	<strong>Participation</strong>
</p>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>
		<strong>Projects:</strong> This may include, but is not limited to: craftsmanship, application of
		skill, use of classroom time, growing and improving artistic skills, photographing, and submitting
		completed assignments.
	</li>
	<li>
		<strong>Participation:</strong> This may include, but is not limited to: sketches, video/article
		reviews, class critiques, self reflections, portfolio building, studio maintenance/safety, and group
		discussions.
	</li>
</ul>
