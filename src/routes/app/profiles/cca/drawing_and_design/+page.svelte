<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This is an introductory level studio art course that will provide a solid foundation for any other
	visual arts class here at CCA. Special emphasis will be placed on developing basic drawing skills
	and approaches to 2-Dimensional design issues, including lessons in observational drawing,
	composition, and the Elements of Art and Principles of Design. Students will have the opportunity
	to explore a variety of tools, materials, and techniques and learn all about creative problem
	solving. Additionally, we will look at influential contemporary and historical artists for
	inspiration and to better understand the context of artmaking throughout history.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This is a studio course meaning that most work will be completed during class time and will be
	project based. This is not a homework intensive course, but requires good attendance, a strong
	work ethic, and willingness to try new things every day. Homework might consist of sketching,
	finishing projects, and/or bringing in objects for observational drawing. Grading for Drawing &
	Design classes typically consists of projects, studio performance, and classwork. Specific
	weighting of assignments is determined by each individual instructor.
</p>
