<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">Play a traditional woodwind, brass, or percussion instrument.<br>This is an audition-recommended ensemble. If you are unsure you will be prepared for this course, please schedule an audition with Ms. Villanova before selecting the class.<br>Recommended to take year long/both terms.</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This is the entry level band course at CCA; however, it is not a “beginning course”. Students wishing to start an instrument are encouraged to take lessons in order to prepare them for the level of this course.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed to provide the opportunity for in-depth study in the areas of solo, chamber music, and band literature as it applies to their instrument. This will be accomplished through research, written and oral presentations, and performance. Through this study students will understand the cultural and artistic relevance of particular compositions throughout history, and the role of the musician in a given societal context.
</p>
<ul class="mr-4 mb-4 text-xl 2xl:w-3/4 list-disc ml-6">
	<li>Musical concepts: tone, intonation, articulation/sticking, individual technique, ensemble techniques</li>
	<li>Performances: Students will perform on campus and in the community as a part of this class. These performances are generally on evenings and weekends. Students will be notified well in advance of these dates.</li>
	<li>Symphonic Band and Wind Ensemble may meet simultaneously and will accommodate advanced players with pull out collaborations.</li>
</ul>

<h2 class="mt-8">Grading</h2>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>Classwork: 50%</li>
	<li>Practice reports (2.5 hours per week required): 20%</li>
	<li>Playing tests: 20%</li>
	<li>Written work/music theory: 10%</li>
</ul>
