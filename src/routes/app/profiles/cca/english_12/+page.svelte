<h3 class="mt-6 text-2xl">Workload Expectations for a 12th Grade English Student</h3>
<p class="mr-4 text-xl 2xl:w-3/4">
	<strong>NOTE:</strong> Individual work depends on the length and involvement of the assignment, project,
	essay, group project, etc.
</p>
<ul class="mr-4 ml-6 text-xl 2xl:w-3/4">
	<li>
		4-6 major literary works (i.e. novels, autobiographies, plays, etc.) or units (i.e. short
		stories, essays, poetry, etc.) –being assigned 20-30 pages of reading per class period (note:
		when work is being studied/so not every night.)
	</li>
	<li>1-2 pages of written response to the literary works/units (take home or in-class)</li>
	<li>
		1 college essay – a reflective essay (can be used as an entrance essay on a college application)
	</li>
	<li>
		1 college level multi-draft research essay – 7-10 pages (done in many structured stages to
		acclimate the student to college writing)
	</li>
	<li>
		Business Letter and Resume Writing (in relation to completing a 15 hour community internship/job
		shadowing relating to their essay topic)
	</li>
	<li>
		2-3 prepared speeches culminating in a 7-10 minute speech before a community panel mirroring a
		job interview or presentation
	</li>
	<li>1-2 projects (group or individual)</li>
</ul>

