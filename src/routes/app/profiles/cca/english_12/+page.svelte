<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	English 12 is a one-term course focused around a structured "Senior Project" where students
	identify their interests and pursue their goals through research, writing, and community
	participation. Students will also be given instruction, guidance, and time to construct/develop
	their college essays—preparing the student for their upcoming college Freshmen Composition
	classes. Students will read major works, such as Shakespeare, short stories, poetry, and seminar
	novels while infusing mythological and literary allusions. Students will use critical thinking
	skills and apply them to assignments and projects given in class. Students will work in
	cooperative-learning groups in which they share ideas and concepts from the works read in class.
	Students will leave this class prepared for college and business reading, writing, and speaking
	challenges.
</p>

<h3 class="mt-6 text-2xl">Necessary Skills</h3>
<p class="mr-4 text-xl 2xl:w-3/4">
	Upon entering the course, students should possess the following skills:
</p>
<ul class="mr-4 ml-6 text-xl 2xl:w-3/4">
	<li>Comprehension & analysis of grade-level text</li>
	<li>Analysis of non-fiction, expository text</li>
	<li>Literary response & analysis</li>
	<li>Writing a process paper (multi paragraph)</li>
	<li>Creating a clearly stated thesis statement / presenting an argument</li>
	<li>Develop main ideas with supporting evidence</li>
	<li>Evaluate the credibility of an author's argument</li>
	<li>Blend quotations & textual evidence artfully</li>
	<li>Support perspectives & judgments with convincing evidence</li>
	<li>Deliver presentations</li>
	<li>Contributes to class discussions by asking questions & takes discussion into new realms</li>
	<li>Understand sentence construction and proper English usage</li>
	<li>Control of grammar, paragraph and sentence structure, diction, syntax</li>
	<li>
		Manuscript requirements: title page, pagination, spacing and margins, integration of source and
		support material with appropriate citations
	</li>
</ul>

<h3 class="mt-6 text-2xl">Description of a 12th Grade Student</h3>
<ul class="mr-4 ml-6 text-xl 2xl:w-3/4">
	<li>Interests may lie in other curricular areas</li>
	<li>Diligent, hard working, self-motivated</li>
	<li>Masters concepts quickly; appreciates thorough presentation and practice of new concepts</li>
	<li>Keeps a well-organized notebook, take notes, and work in groups</li>
	<li>
		Enjoys challenges, but may need some assistance with complex, critical-thinking and non-routine
		problems
	</li>
	<li>
		Prefers to be shown a problem solving strategy and will learn it and be able to apply it to new
		problems
	</li>
	<li>Feels comfortable when shown a method that he or she can master</li>
	<li>
		Is comfortable in an environment of sharing ideas as a medium for a discussion of deeper
		analysis and written and oral communication
	</li>
</ul>

<h3 class="mt-6 text-2xl">Performance Objectives</h3>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students will be able to demonstrate mastery in the following areas:
</p>
<ul class="mr-4 ml-6 text-xl 2xl:w-3/4">
	<li>Comprehension & analysis of grade-level text (fiction and non-fiction material)</li>
