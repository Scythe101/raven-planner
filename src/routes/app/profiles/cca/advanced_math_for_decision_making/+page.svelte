<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Content and Expectations</h3>
<p class="w-3/4 text-xl">
	Multi-Criteria Decision Making Some decisions involve comparing alternatives that have strengths
	or weaknesses with regard to multiple objectives of interest to the decision maker. For example,
	your objectives in buying health insurance might be to minimize cost and maximize protection.
	Sometimes multiple objectives like these get in each other’s way. Multi- criteria decision making
	is a structured methodology designed to handle the tradeoffs among multiple objectives.

	<br />
	<br />
	Mathematical Programming: Mathematical programming is a method of modeling a decision context using
	a system of linear inequalities representing constraints and a linear function representing the objective
	of the decision. Mathematical programming is relevant when there is a single objective to be optimized.
	For example, a model can be developed to decide about which mix of products to manufacture to maximize
	profits. More applications are possible when the possible values of the decision variables are limited
	to the integers or even further restricted to zero and one. Solving mathematical programming problems
	requires long and tedious matrix calculations, so we will use a spreadsheets and specialized software
	to find optimal solutions to our models.
	<br />
	<br />
	Sensitivity Analysis: A great deal of time and energy will be spent interpreting the results of our
	mathematical models. We will investigate how changes in the parameters of the problem affect the optimal
	solution and the optimal value of the objective function. This interpretation and analysis will always
	be tied to the real-world context of the problem.
	<br />
	<br />
	Basic Probability and Randomness: We will strive to develop a better understanding of random patterns
	through the use of simulated experiments. Physical simulations and electronic random number generators
	will be used for simulate different types of events. Basic concepts and notation of probability and
	sets will be introduced.
	<br />
	<br />
	Conditional Probability: People have notoriously poor intuition about conditional probability (i.e.,
	the probability that an event will occur given that a different event also occurs). We will develop
	concepts related to conditional probability through analyzing data, problem contexts, and partitions
	of sets. This will eventually lead to a more
	<br />
	<br />
	formal definition of conditional probability and related formulas. Decision Trees: Decision trees provide
	a structure for determining the alternative that optimizes the expected value. The decision tree also
	provides the probability distribution for each of the alternatives. Binomial and Geometric Distributions:
	The concept of a probability distribution function is introduced. When repeated random events follow
	the same assumptions, there may be a formula that summarizes the probabilistic pattern. This formula
	will have parameters whose values vary from context to context. The binomial probability distribution
	calculates the probability of a certain number of successes in a given number of trials. The geometric
	distribution tracks the number of repetitions until the first success.
	<br />
	<br />
	Poisson Distribution: The Poisson distribution is used to characterize a probabilistic environment
	in which random events occur totally independent of one another. The distribution is used to estimate
	the probability that there will be X events in a unit of time.
	<br />
	<br />
	Normal Distribution: The Normal distribution is used to describe a continuous random variable with
	a mean of μ and a standard deviation of σ. The sum of identically distributed independent random variables
	is known to approach the normal distribution. For that reason, the randomness of the sample average
	of a random variable can be approximated with the normal distribution.
</p>

