<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	AP Art History is an art course designed to provide high school students with the benefits of an introductory college course in Art History. Students will explore European Art (Mesopotamian to Renaissance to Contemporary Art), Oceanic, African, Islamic, Asian, Eastern, and American art within a social, historical, and cultural context. We will spend most of the time in class analyzing examples of architecture, sculpture, painting, as well as other forms of art. In addition, we will create art based on our study of art history and make a visit to local museums of art (budget permitting).
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The goal of this class is not only to enjoy, write about, and talk about art, but also to take the AP Art History Exam. Students who achieve this goal may earn advanced placement and/or credit at many colleges and universities.
</p>

