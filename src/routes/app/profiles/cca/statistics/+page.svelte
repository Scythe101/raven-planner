<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	This course teaches students how to use four-steps of the statistical process: ask questions, collect
	data, analyze data, and make conclusions.
	<br />
	Major statistical topics include: analyzing distributions of univariate and bivariate data, both
	categorical and numerical, using graphs and summary statistics; correlation and least squares
	regression; using simulations to estimate probability distributions; theoretical probability
	distributions, including the binomial and normal distributions; rules of probability, including
	conditional probability and expected value; the logic of hypothesis testing, including stating
	hypotheses, calculating and interpreting p-values, drawing conclusions, and Type I and Type II
	errors; using confidence intervals to estimate parameters; and proper methods of data collection,
	including sampling and experimentation. Use of technology, including online applets and the
	graphing calculator will be prominent in the course.
</p>

