<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Through this course, students explore, understand, create, evaluate, and criticize dance as an art form. In historical and cultural contexts, students develop skills that include kinesthetic awareness, theories of choreography, dance technique, and multisensory integration. Using the dance elements of time, space, and energy, they create and communicate meaning through dance improvisation, composition, and performance. Students analyze, compare and contrast their work, evaluate their work, and critique their development as an artist. There will be master classes in improvisation, somatics, modern, ballet, jazz, hip hop, as well as rehearsal for Senior Thesis projects. Students may also be presented the opportunity to teach in an outreach setting.
</p>

