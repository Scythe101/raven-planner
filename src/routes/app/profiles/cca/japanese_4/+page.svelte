<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in Japanese 3</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The successful student in this continuing course will be able to compare, explain, suggest and express life experiences. In addition to all "hiragana" characters and "katakana" characters, the student will be able to read and write approximately 250 "kanji" characters. This class is in alignment with national standards at the intermediate-low level.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Activities:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Typical activities include Japanese conversation, intrapersonal presentations, typing an essay in Japanese, textbook exercises, language-focused games, explaining Japanese cultural practices—such as the importance of gift giving; comparing Valentine's Day to White Day holidays in Japan; tea ceremony; Ikebana (flower arrangement); creating a travel brochure and itinerary; persuading others to visit a prefecture in Japan and backing up persuasive speeches with real facts (Common Core lessons); presenting a dream house with cultural practices—such as taking off shoes before entering the house—and products such as a kotatsu (heated table with blanket), tatami mats, Ikebana, etc.
</p>

<h2 class="mt-8">Grading</h2>
<h3 class="font-dm-serif-display mt-4 text-2xl italic">Workload:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Outside of class, the student will review notes, practice vocabulary, drill character flashcards, thoughtfully complete written assignments and work on projects collaboratively and individually. In general, the successful student will spend approximately 30-60 minutes on work outside of class to prepare for the next class session.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Assessment:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Assessments include oral presentations, dialogues, essays, projects, quizzes and tests.
</p>

