<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The Cinema Conservatory is an audition-based, after-school extension of the daytime Envision Cinema program, offering smaller class sizes and more individualized instruction for the most advanced film students. The Cinema Conservatory offers an active approach to filmmaking technique and theory. Workshops intend to build upon and supplement daily production experience and are taught by a variety of practicing artists. The focus of the Conservatory is narrative or documentary video production. Students develop original screenplays, pitch their ideas, coordinate, cast, and produce short films in teams and on their own. All projects are intended for submission to student and professional film festivals. Evaluation of students will be linked to successful festival submission. The collaborative nature of the arts ensures that Conservatory students enjoy many opportunities to work with and learn from one another. A focus on interdisciplinary collaboration is best witnessed during the Conservatory-wide event called the Projects. Projects put all Conservatory students on interdisciplinary teams of twelve to fifteen artists for a weekend at the beginning of the year.
</p>

