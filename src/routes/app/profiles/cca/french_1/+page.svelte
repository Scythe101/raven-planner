<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">None</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students who choose to pursue French will embark on a study of the language as well as the rich
	cultures of the French-speaking world.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will keep a well-organized notebook, take notes, listen to the language, work
	independently and in groups, and make oral French presentations. Listening and speaking are
	practiced in class and in a Language Laboratory (where available). Additionally, students complete
	activities and conduct research using the Internet and they use Google Classroom to access
	resources and keep informed.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Successful French students come to class ready to learn with a positive attitude, an open mind and
	a willingness to take risks in order to learn a new language, to ask questions, speak, read, write
	and listen at a novice level.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Student Outcomes:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	After the completion of this introductory course, students will be able to engage in simple
	conversations on the topic of their likes/dislikes, school life, family, home, food, clothing, and
	travel. They will be able to compose autobiographical pieces and other projects related to these
	topics and they will hear/read about the daily lives of young francophone students.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Culminating Activities:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Culminating activities may include presenting a fashion show, completing creative writing/dialogues
	based on real-life scenarios, electronic projects about students' real- or dream home, a family tree
	accompanied by 10-15 sentences about students' families, using target vocabulary, adjectives and
	verbs in the present tense.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Focus:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course places an emphasis on listening comprehension, vocabulary development, the
	fundamentals of verb conjugation in the Present Tense of the indicative mood as well as the Past
	Tense called Passé Composé, and the formation of simple, complete and grammatically correct
	French sentences.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	This class is aligned with national standards at the novice level.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Outside of class, the student will complete nightly assignments for a duration of approximately 30
	minutes to prepare for the next class session.
</p>
<p class="text-xl 2xl:w-3/4">
	Assignments include oral presentations, dialogues, short compositions, projects, quizzes and tests.
</p>
