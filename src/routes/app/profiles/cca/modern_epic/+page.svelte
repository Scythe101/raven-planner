<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	The purpose of this course is to reinforce the analytical skills students are learning in their
	English and social studies classes. Such ideas as characterization, structure, world-building, and
	the individual within society will be explored. Students will produce work citing materials in
	film and text analyzing, comparing, and contrasting Tolkien's and Martin's views of the
	individual's role within a larger world as well as examining how each writer's work reflects the
	social, historical, and psychological characteristics of the modern world.
</p>

<p class="mr-4 text-xl 2xl:w-3/4">
	This course will explore <em>The Hobbit</em> and <em>Lord of the Rings</em> films and compare them
	to George R. R. Martin's series of books collectively titled <em>A Song of Ice and Fire</em>.
	Students will read all five of Martin's texts over the course of the term. Students will be
	introduced to elements of the epic from its ancient beginnings in Mesopotamia, Greece, and Rome,
	and will analyze the changes in the epic form in the twentieth and twenty-first centuries,
	including, but not limited to, the evolution of the epic hero and his or her world, the role of
	religions, the greying of the distinction between good and evil, and the ideas of Joseph Campbell
	and the quest of the hero, and Jungian archetypal psychology. Students will study excerpts from
	Joseph Campbell's <em>Hero with a Thousand Faces</em>, James Frazer's <em>The Golden Bough</em>,
	and Thomas Malory's <em>Morte d'Arthur</em>.
</p>

<p class="mr-4 text-xl 2xl:w-3/4">
	Attention will also be given to the techniques of film and adaption, specifically with a study of
	Peter Jackson's representation of Middle-earth. An exploration of how cultures produce, consume,
	and pass on the epic through time and tradition will also be analyzed, from the oral bards of
	Ancient Greece to the modern American culture of film. While some familiarization with Tolkien
	might be helpful, it is in no way required to participate in the course.
</p>

<p class="mr-4 text-xl 2xl:w-3/4">
	One of the overarching areas for analysis will be various perspectives on what is meant by the
	ideas of hero and heroism as they are displayed in myriad cultures and texts.
</p>

<p class="mr-4 text-xl 2xl:w-3/4">
	<strong>This course is designed for serious readers.</strong> In order to complete Martin's five novels
	within the time-frame of the course, students will need to read 35-40 pages per night.
</p>

