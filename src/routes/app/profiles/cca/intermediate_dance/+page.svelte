<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed for dancers who have mastered the basic technical aspects of dance. Students continue their exploration of dance techniques including ballet, jazz, modern, and hip hop, and are also challenged on the elements of body alignment, placement and articulation. The discussions of maintaining good health and physical well being as it pertains to the art of dance are continued. Dancers in Intermediate explore movement quality and accuracy. The element of sequencing becomes more important with the ultimate goal of preparing students for a more advanced dance course. <b>Instructor approval is required for enrollment in Intermediate Dance.</b>
</p>

