<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	English 10 Honors is a one-term course organized around the study of world literature with the
	theme of "Journey into the World." It includes short stories, expository texts, novels, drama and
	poetry. Students will be challenged and given the opportunity to grow in their practical language
	skills through meaningful class discussions, journal prompts, essay writing, creative projects,
	oral presentations, theater arts. Students will continue to use and practice grammar, punctuation,
	and vocabulary skills. With an understanding of the fundamentals of research elements, students
	will construct multi-paragraph essays that respond to expository texts, analyze literature,
	persuade, and narrate events, as well as experiment with other written forms, such as business
	letters and technical documents. Analysis is key as students learn to go beyond rephrasing and
	defining into looking at the significance of the elements of a piece of writing. Ultimately,
	students will write research papers in which they investigate and research multiple sources, then
	analyze those sources to create a unique thesis statement and provide evidence to support the
	argument. Because additional works are added, students should expect a faster pace when addressing
	this curriculum. The writing focus is designed to prepare students for the SAT, AP Exams, and
	college level writing requirements.
</p>

