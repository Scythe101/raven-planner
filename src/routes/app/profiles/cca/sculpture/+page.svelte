<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Sculpture is an intro level studio art class that explores 3D forms and focuses on creating work in various related techniques and processes. Through the imaginative use of models, drawings, photographs, traditional and contemporary art history, students will create three-dimensional sculptures. Sculpture class is a very hands-on experience; you will be working primarily with clay. Students will create functional pieces (mugs, bowls, vases, etc.) and sculptural works of art. Students will have an opportunity to learn to throw on the wheel, and will also dabble in a variety of other 3D materials to create unique works of art. Students apply the elements of art, principles of design, as well as drawing and painting skills. In order to be successful in this class, it is recommended that you first take Drawing and Design.
</p>

<h2 class="mt-8">Grading</h2>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
