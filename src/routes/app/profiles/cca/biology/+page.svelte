<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	College Prep Biology: In the Living Earth allows students to move between big picture concepts and
	more detailed and mechanistic understandings of life science and related earth science topics. The
	exploration of topics is designed to develop student understanding in a rigorous and accessible
	way that builds upon prior knowledge from middle school and the previous units in this course.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Beginning with the observable world provides students a conceptual foundation to investigate and
	build understanding of the invisible world. This approach aims to establish scientific awareness
	and literacy through the lens of biology.
</p>

