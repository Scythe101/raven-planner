<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	AP Physics C: Mechanics (Quarter 1) and Electricity and Magnetism (Quarter 2) is designed for
	students who have successfully completed both AP Physics 1 and AP Physics 2 and are considering
	careers in science, technology, engineering and/or mathematics (STEM). This class covers more
	advanced study of classical mechanics and electromagnetism, focused on problem solving of
	university-level physics problems using calculus and prepares students for College Board AP
	Physics C exam.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students will explore these topics through discussions, laboratory investigations, teacher
	demonstrations, and in-class assignments. Per CollegeBoard, introductory differential and integral
	calculus is used throughout the course.
</p>

