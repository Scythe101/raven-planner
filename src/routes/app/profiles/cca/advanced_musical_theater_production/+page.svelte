<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course is designed to give students an advanced and practical understanding of the American art form of musical theater. Students will receive advanced training in audition techniques, directing, and writing musical theater, as well as musical theater theory. Students will further their skills in vocal and movement techniques and will work together to produce a full, original piece of musical theater. Upon completing this class, students will have the skills to enter a career in professional music theater.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	All students will be expected to participate heavily in the singing and dancing portion of the class, and will be called upon to direct beginning level students. Students must take the prior level Musical Theater Production course before taking Advanced Musical Theater Production.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The purpose of Advanced Musical Theater Production is to further develop the skills of an advanced musical theater student. Focus will be on mastery of vocal techniques, the ability to effectively block and choreograph a scene/song, and the understanding of the structure of a musical. Students will learn to create, produce, and market an original piece of musical theater. This is NOT a beginning level class.
</p>

