<h2 class="mt-8">Course Content and Expectations</h2>
<p class=" mr-4 text-xl 2xl:w-3/4">
	In Integrated Math 1 Honors, students will go deeper into grade level standards as well as several
	Pre Calculus and Integrated Math 2 standards. Student assignments will contain more critical
	thinking and have a higher depth of knowledge and more performance tasks. Students will learn
	concepts such as:
</p>

<ul class="font-noto-serif mr-4 ml-6 list-outside list-disc text-xl 2xl:w-3/4">
