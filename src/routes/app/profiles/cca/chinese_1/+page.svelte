<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Chinese 1 is a rigorous academic course that takes an integrated communicative approach to
	empower students to speak Chinese (by mastering the Pinyin system, communicating through
	accurate pronunciation and intonation), to listen to Chinese (understand simple everyday
	narratives and dialogues), to read Chinese (comprehend simple texts on personal and social
	interactions using context clues, sentence structure, and identifying details), to write Chinese
	(mastering strokes, stroke order, radicals, and writing traditional characters and simplified
	characters), and to type Chinese by using pinyin input.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The objective of this course is to equip you with skills in speaking and listening to Mandarin and
	reading and writing in Chinese. You will leave the course equipped with the ability to conduct
	basic conversations in Mandarin in everyday and formal situations and to write/type and read a
	set of 200 to 300 simplified and traditional characters. The five skills (speaking, listening,
	reading, writing, and typing) will be integrated throughout this course. Vocabulary, language
	patterns and grammar are introduced and applied in the context of practical communication.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Traditional/Simplified Characters:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Chinese characters may either be written/typed in the simplified (简体) or the traditional system
	(繁體). The simplified system is used in Mainland China, while the traditional style is used in
	Hong Kong, Taiwan, and overseas Chinatowns. For Chinese 1, all students write both traditional
	and simplified Chinese characters. Textbooks will be printed in both simplified and traditional
	Chinese and the character workbooks are in both traditional and simplified Chinese.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Whenever writing characters, we write both sets of characters in all examples, text materials, and
	handouts. The aim is for all students in the first year to learn, recognize and write both character
	sets. This is important from a number of perspectives. For instance, from a linguistic perspective,
	many sounds are linked to radicals and characters in both traditional and simplified scripts and
	also since our students come from a variety of backgrounds, we hope to encourage a variety of
	heritage and Chinese-character (like students who have taken Japanese) learners.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Finally, our curriculum follows the standards recommended by a majority of colleges and
	universities in the United States including the UC/CSU systems.
</p>
