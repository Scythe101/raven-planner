</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	AP English Literature and Composition is a one-term course. This is a freshman level college
	course in Literature, which skips over the final year of high school English, providing a survey
	of the major literary periods and the significant works of important authors. Students will read
	many critical essays and learn the art of technical poetry analysis Through the close reading of
	selected texts, students deepen their understanding of the ways writers use language to provide
	both meaning and pleasure for their readers. As they read, students consider a work's structure,
	style, and themes as well as such smaller-scale elements as the use of figurative language,
	imagery, symbolism, and tone. Students must be able to read actively. Additionally, students will
	prepare for the AP Literature Exam via timed writing, sample tests, and lecture. Because the AP
	test is a freshman college level, national test, it allows students to enter college at the
	sophomore level; in effect, AP 12 students are skipping 12th grade English.
</p>

