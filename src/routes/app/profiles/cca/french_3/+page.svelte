<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">C- (70%) or better in French 2</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students entering French 3 should have a command of the following concepts: present-tense verb
	conjugations (regular and irregular forms); regular and irregular verbs in the passé composé
	(formed both with avoir and être); articles (un, une, des, du, de la, des, de l'); and vocabulary
	presented in French 1 and French 2.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students should also be able to write a brief paragraph in French on a given topic (e.g., What did
	you do last weekend?, Describe the last movie you saw, What are your plans for the summer?) and
	comfortably respond to questions orally in French.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course places an emphasis on correct pronunciation, listening comprehension, vocabulary
	development, the verb conjugations in present, past, future and conditional tenses, and the
	formation of well thought-out, correct French compositions.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Grammar Concepts:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In French 3, students will learn concepts such as expanded vocabulary for talking about home,
	health, friends, family, school, entertainment, travel, shopping, daily routine; correct verb forms in
	present, passé composé, imparfait with an emphasis on past tenses used together; future and
	conditional tenses; the subjunctive; object pronouns; relative pronouns; and negative forms.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Course Activities:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In French 3, students will write compositions in French on a variety of socially, culturally and
	personally relevant issues; practice spoken French in the Language Listening Laboratory and at
	home by recording on an audiocassette as well as presenting orally on a variety of topics;
	participate in class discussions in French; present skits and dialogues in French; improve reading
	comprehension of longer, more challenging material, gradually moving towards an authentic full
	text in French; explore French literature and history; and conduct Internet activities and cultural
	research both independently and in small groups.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	This course is in alignment with national standards at the intermediate-low level.
</p>

<h2 class="mt-8">Grading</h2>
<h3 class="font-dm-serif-display mt-4 text-2xl italic">Workload:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will be expected to spend an average of approximately 45 minutes to an hour outside of
	class on homework for each class period, preferably done in shorter daily sessions.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Assessment:</h3>
<p class="text-xl 2xl:w-3/4">
	Exams, quizzes, projects, writing assignments, homework and oral participation.
</p>
