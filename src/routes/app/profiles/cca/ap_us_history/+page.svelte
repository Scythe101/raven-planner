<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	The AP United States History (U.S. History) course at Canyon Crest Academy is designed to prepare
	students for the content demands of the AP U.S. History exam, offered by the College Board. The
	course follows the standards prescribed by the College Board in the AP Course Description:
	https://apcentral.collegeboard.org/pdf/ap-us-history-course-and-exam-description.pdf
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	The course spans American history from the founding of pre-Columbian contact to approximately
	2004. The course emphasizes the study of four distinct modes of historical analysis: political
	institutions, behavior, and public policy; social changes, cultural, and intellectual
	developments; diplomacy and international relations; and economic developments. Students read from
	a college-level textbook and learn to analyze primary-source & secondary source documents from the
	time periods studied. Students may wish to take the AP U.S. History exam. Successful completion of
	this exam can result with students earning college credit, depending on the policies of individual
	colleges and universities.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	At Canyon Crest Academy, we have designed our AP U.S. History curriculum with the unique demands
	of the 4x4-block curriculum in mind. Our primary objective is threefold:
</p>
<ol class="font-noto-serif ml-6 list-outside list-decimal text-xl 2xl:w-3/4">
	<li>To instruct students with the full content of the course in an abbreviated period of time</li>
	<li>To introduce students to various skills that are necessary for passage of the AP exam</li>
	<li>
		To provide a curriculum that will encourage students to think like a historian by engaging them
		in a variety of primary and secondary sources.
	</li>
</ol>

