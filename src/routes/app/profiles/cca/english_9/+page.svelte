<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	English 9 is a one-term course with a focus on various genres of literature with a central theme
	of "The Individual and Society." Students will be reading and studying novels, expository texts,
	short stories, autobiographies, drama, and poetry. Students will be challenged and given the
	opportunity to grow in their practical language skills through meaningful class discussions,
	journal prompts, essay writing, creative projects, oral presentations, and theater arts. Students
	will continue to use and practice grammar, punctuation, and vocabulary skills. With an
	understanding of the fundamentals of literary analysis, students will be able to construct a
	multi-paragraph essay as well as experiment with other essay forms and modes. The writing focus is
	designed to prepare students for the SAT, CAHSEE, and college level writing requirements. Students
	will develop critical thinking skills based on teacher modeling and will apply them to assignments
	independently and in cooperative, learning groups.
</p>

<p class="mr-4 text-xl 2xl:w-3/4">
	<strong>NOTE:</strong> Homework frequently includes reading and often writing. Homework lengths will
	differ according to the student's reading level and writing proficiency.
</p>

<h3 class="mt-6 text-2xl">Necessary Skills</h3>
<p class="mr-4 text-xl 2xl:w-3/4">
	Upon entering the course, students should possess the following skills:
</p>
<ul class="mr-4 ml-6 text-xl 2xl:w-3/4">
	<li>Able to write a process/multi-paragraph paper</li>
	<li>Develop main ideas with supporting evidence</li>
	<li>Support perspectives & judgments with convincing evidence</li>
	<li>Speak effectively and deliver presentations</li>
	<li>Use varied sentence types</li>
	<li>Use parallelism to present items in a series</li>
	<li>Use subordination, coordination, apposition to indicate relationship between ideas</li>
	<li>Use standard punctuation, spelling and capitalization</li>
</ul>

