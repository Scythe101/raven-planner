<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Exploring Computer Science provides students with a comprehensive understanding of cloud-based software development, human-computer interaction (UI/UX), and the essential tools and techniques used in the field. Throughout the course, students will learn real-world software development tools such as Figma and Git, engage in hands-on projects, apply industry-standard practices, and develop their web applications using Google Firebase. By the end of the course, students will have the skills and knowledge needed to create user-friendly web applications, manage software development projects, and utilize cloud computing effectively. They will use these skills to plan, create, and pitch their own web applications in the Web Application Final Project.
</p>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	A variety of assessments will be used including projects, assessments, and write-ups. Students will submit projects at the end of some units, which will be assessed based on the successful implementation of the required elements from the unit, demonstration of knowledge, and additional effort to create quality work. Quizzes will be based on in-class lecture material, demonstration of taught skills, or assigned reading. During the Web Application project, grading will be based on group participation, stand-up meetings and progress reports, the final product and its subsequent presentation. Web Applications will be graded based on quality, demonstration of effort, presentation, and original thinking and creativity.
</p>

