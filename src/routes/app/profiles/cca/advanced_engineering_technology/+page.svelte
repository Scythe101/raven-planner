<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">Introduction to Engineering & Digital Electronics OR Exploring Computer Science OR APCSA</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This capstone course in the Engineering and Architecture CTE pathway at CCA provides students with advanced, hands-on experience in engineering technology. Students will engage in the full design and development process, including problem identification, research, requirements, agile project planning, design, build, test, and presentation. The course emphasizes independent and group work, problem solving, and project-based learning. Students will use industry-standard software such as Autodesk Inventor, Autodesk Revit, LoggerPro, MDsolids, Scilab, Eclipse, Android Studio, and Eagle Board Designer. Industry professionals review the course annually to ensure relevance. Students will showcase projects to local businesses and community members each February.
</p>

<h2 class="mt-8">Units of Study & Projects</h2>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4 mb-4">
	<li>Introduction to the Design and Development Process</li>
	<li>Problem Identification (Proposal - Problem to solve)</li>
	<li>Research (How others have solved problem, patent searches)</li>
	<li>Decision Process and Requirements (Proposed project, Systems requirements document, Derived requirements document, Acceptance test plans)</li>
	<li>Agile Processes (Project plan)</li>
	<li>Design (3D model, schematics, simulation)</li>
	<li>Build (Fabrication of proposed project)</li>
	<li>Test (Verification of project against acceptance test plan)</li>
	<li>Presentation (Demonstration, Report)</li>
</ul>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	A variety of assessments will be used including journal entries, projects, and self and peer assessments. Example assessments include project plan and schedule updates, demonstration of mechanical, electrical, and control mechanisms, and self-evaluation of project design.
</p>

