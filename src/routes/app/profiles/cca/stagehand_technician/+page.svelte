<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course provides entry level and upgrade level training in the skills required for technical theatre operations. Instruction covers safety, theatre architecture and stage equipment, production organization and management, and design theory and practical application in scenic, props, lighting, sound, and costumes. Students use equipment including light construction hand- and machine tools, painting equipment and supplies, sound board equipment, electrical lighting equipment and controls, and pulley and rigging equipment. ROP courses are required to spend a minimum of 10 hours on training in job-seeking and retention knowledge and skills.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	The purpose of this class is to introduce students to technical theatre through classroom theory and hands-on training, in which students will assist in providing safe and efficient production support for the various shows that will take place at the school. This class also aims to prepare students for working in technical theatre in a non-academic setting. This class is ideal for aspiring performers, artists, sound technicians, musicians, and filmmakers who want to know how things work behind-the-scenes and how to apply their knowledge and skills in the real world.
</p>

