<h2 class="mt-8">Course Description</h2>
<p class="mr-4 text-xl 2xl:w-3/4">
	English 9 Honors is a one term course with a focus on various genres of literature with a central
	theme of "The Individual and Society." Students will be reading and studying novels, expository
	texts, short stories, autobiographies, drama, and poetry. Honor students will be expected to
	possess independent critical thinking skills and to function at a faster pace, as more works of
	literature will be covered. Students will be challenged and given the opportunity for continued
	growth in their practical language skills through meaningful class discussions, journal prompts,
	essay writing, creative projects, oral presentations and theater arts. Students will continue to
	use and practice grammar, punctuation, and vocabulary skills. With an understanding of the
	fundamentals of literary analysis, students will be able to construct a multi-paragraph essay as
	well as experiment with other essay forms and modes. The writing focus is designed to prepare
	students for the SAT, CAHSEE, and college level writing requirements. Students will develop
	critical thinking skills based on teacher modeling and will apply them to assignments
	independently and in cooperative, learning groups.
</p>

<p class="mr-4 text-xl 2xl:w-3/4">
	<strong>NOTE:</strong> Homework frequently includes reading and often writing. Homework lengths will
	differ according to the student's reading level and writing proficiency.
</p>

<h3 class="mt-6 text-2xl">Necessary Skills</h3>
<p class="mr-4 text-xl 2xl:w-3/4">
	Upon entering the course, students should possess the following skills:
</p>
<ul class="mr-4 ml-6 text-xl 2xl:w-3/4">
	<li>Thorough comprehension & critical analysis of grade-level text</li>
	<li>Critiquing non-fiction & expository text</li>
	<li>Writing critical analysis essays</li>
	<li>Develop main ideas with supporting concrete evidence</li>
	<li>Construct a thesis that presents an educated argument and connects thematic ideas</li>
	<li>Use parallelism to present items in a series</li>
	<li>Logically support perspectives & judgments with convincing evidence</li>
	<li>Deliver well-planned presentations with sophisticated use of varied sentence types</li>
	<li>Consistent use of parallelism to present items in a series</li>
	<li>
		Consistent use of subordination, coordination, apposition to indicate relationship between ideas
	</li>
	<li>Consistent use of standard punctuation, spelling and capitalization</li>
</ul>

