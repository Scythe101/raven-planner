<h2 class="mt-8">Course Description</h2>
<p class=" mr-4 text-xl 2xl:w-3/4">
	In AP Statistics students will learn how to analyze data (exploration of data, describing
	locations in a distribution, examining relationships) and how to produce data (from surveys,
	observational studies, and experiments). They will also learn the foundations for inference (which
	include probability, random variables, geometric and binomial distributions, sampling
	distribution), and finally, inference (confidence intervals and a variety of significance tests).
	<br />
	<br />
	Students will find that there is more reading and analysis than in previous math courses; pacing is
	fast and the course requires a strong math background. There are a variety of projects, and due to
	the technical nature of the course, students will be required to learn a variety of statistical functions
	on a graphing calculator.
	<br />
	<br />
	An emphasis will be made on group work, reading/writing technically, and data/graph analysis. This
	course is weighted.
</p>

