<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Turn your passion for video games, art/graphic design or technology into a future in the multi-billion-dollar video game industry. A wide range of skills will be developed in the class including storyboarding, 3D modeling, rendering, and programming using a variety of software. Once we develop a strong foundation in game design we will focus on creating characters, background maps, and animations to import into various 2D and 3D game editors. This class will be taught through individual/team projects and hands-on assignments. The class culminates with a team game developed over three-weeks and presented to the class.
</p>

<h2 class="mt-8">Objectives</h2>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4 mb-4">
	<li>Programming skills: foundational knowledge of programming, basic animation and game design using p5.js and p5Play</li>
	<li>C# Skills: foundational knowledge of programming in C# and confidence to implement new features</li>
	<li>Unity Skills: confidence to create anything in Unity given time and resources</li>
	<li>Project Management: manage personal projects from concept to completion, including outlining, milestones, and tracking progress</li>
	<li>Unity Certified User Exam: skills and confidence to pass the Unity Certified User Programming exam</li>
	<li>Sketchup for Schools Skills: model 3D game objects and export for use in Unity</li>
</ul>

<h2 class="mt-8">Grading</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Course assessment is mainly project based. Students will create a set of structural and design elements in their game in each unit. At the end of each unit, students will submit a project, which will be assessed based on the successful implementation of the required elements from the unit.
</p>

