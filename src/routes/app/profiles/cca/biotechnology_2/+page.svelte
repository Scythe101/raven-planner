<h2 class="mt-8">Prerequisites</h2>
<p class="text-xl">Completion of Biotechnology 1 (B or better)</p>

<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	This course serves as the Capstone course for CCA's Biotechnology CTE Pathway. The purpose of this
	capstone course is to have students engage in advanced lab investigations and projects that build
	upon what they learned in the concentrator course (Biotechnology 1). Our goal is to provide
	meaningful experiences within the field of biotechnology and guide students in their journey to
	become collaborative, creative problem solvers for tomorrow's world.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students will deepen their understanding of the science and business of biotechnology. We will
	focus on fundamental concepts, principles, and practices of biological research and its
	interaction with chemistry, physics, mathematics, engineering, business, and information
	technology. Learning will be in the form of student-centered activities such as laboratory
	investigations, presentations, whole-class discussions, and project-based learning.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Students will have opportunities to explore and evaluate career and post-secondary education
	opportunities in the field of biotechnology. The capstone activity will consist of students
	completing an independent science research project and showcasing their skills and experiences in
	an interview with local biotech organizations for summer internship opportunities.
</p>

