<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Mandarin Chinese 5 will provide students a wider, more in-depth and rigorous study of Chinese
	language and culture and prepares students for advanced Chinese study. It is an intermediate low
	to mid course that follows what students have acquired and learned from Mandarin Chinese 4.
	Students will continue to increase their knowledge and proficiency level of communication in
	interpersonal situations, informal settings, and formal settings that include, but are not limited
	to, extension of classroom language, families and communities, global challenges, contemporary
	life, social justice, Chinese history, etc. with their appropriate cultural responses, and expand
	their knowledge of other disciplines.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Not only will students develop and advance their Chinese communication proficiency, they will also
	develop their understanding of cultural influences, connections, contexts, and distinctive
	viewpoints and in their global competence. Throughout the course, students are provided with a
	variety of opportunities to engage in learning tasks for interpretive, interpersonal, and
	persuasive presentational communication.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	They will also explore aspects of Chinese culture that are related to the language under study,
	compare languages and cultures, and make connections with other subjects and communities.
	Authentic materials in Chinese, such as podcasts, movies, online newspapers and articles, song
	lyrics, and magazines will be used to help students advance their Chinese language proficiency
	level.
</p>

<h3 class="font-dm-serif-display mt-4 text-2xl italic">Traditional/Simplified Characters:</h3>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Chinese characters may either be written/typed in the simplified (简体) or the traditional system
	(繁體). The simplified system is used in Mainland China, while the traditional style is used in
	Hong Kong, Taiwan, and overseas Chinatowns. Students can choose to write either traditional or
	simplified Chinese characters. Textbooks will be printed in both simplified and traditional Chinese
	and the character workbooks are in both traditional and simplified Chinese.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Whenever writing characters, the teacher will write both sets of characters in all examples, text
	materials, and handouts. The aim is for all students to recognize both character sets.
</p>
