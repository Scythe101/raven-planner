<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Organic Chemistry is geared towards students planning a career in the sciences, particularly those
	on a pre-med track. The course is extremely rigorous and taught at the level of a second-year
	college chemistry class. Students who enroll in this course should have a solid foundation in
	college-level chemistry, achieved through the successful completion of both CP/Honors: In the
	Earth System and AP Chemistry.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	Students who successfully complete this course will be able to understand the relationship between
	the chemical structure and functionality of carbon containing compounds, identify typical organic
	reactions and analyze the processes through the kinetic and thermodynamic favorability of various
	mechanisms, explore the syntheses of common organic compounds both in theory and in practice and
	interpret the results of spectroscopic analyses and use those methods to assist in structure
	function group determination.
</p>
<p class="mr-4 text-xl 2xl:w-3/4">
	Each unit of study will incorporate themes of systematic nomenclature, properties based on
	functionalities, typical reactions and reaction mechanisms, and common applications. This course
	will also contain a laboratory component where students will perform a host of classic experiments
	and will communicate their findings via the collection of data in a laboratory notebook and the
	writing of formal reports.
</p>

