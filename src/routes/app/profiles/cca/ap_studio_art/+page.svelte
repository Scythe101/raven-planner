<h2 class="mt-8">Course Description</h2>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	In order to be successful in this class, you need to have taken Drawing and Design, and upper
	level courses such as Painting, Seminar, Advanced Drawing, Digital Art and Design, Advanced
	Digital Art and Design, or Digital Photography. The AP Drawing course addresses sophisticated and
	subtle aspects of design that go beyond the fundamentals, so the teacher will present the material
	assuming you have already taken the prerequisite courses. This is the ideal course for the
	advanced art student who is eager to produce high-level work.
</p>
<p class="mr-4 mb-4 text-xl 2xl:w-3/4">
	“The AP Studio Art Program consists of three portfolio exams—2-D Design, 3-D Design, and
	Drawing—corresponding to the college foundation courses. Portfolios allow flexibility of
	coursework while guiding students to produce college-level quality, artistic investigation, and
	breadth of work. The 2-D Design portfolio addresses two-dimensional design issues and involves
	decision making about how to use the elements and principles of art in an integrative way.
	Students' portfolios demonstrate skills and ideas developed, refined, and applied throughout the
	course to produce visual compositions. Students may choose to submit any or all of the portfolios.
	Portfolios are evaluated based on standardized scoring descriptors aligned with skills and
	understanding developed in college foundation courses.” –AP College Board
</p>
<ul class="font-noto-serif ml-6 list-outside list-disc text-xl 2xl:w-3/4">
	<li>
		Explore a breadth of high caliber work that includes examples of unity/variety, balance,
		emphasis, contrast, rhythm, repetition, proportion/scale, and figure/ground relationship
	</li>
	<li>
		Develop a personal concentration that is an in-depth investigation of a visual idea in concept,
		composition, and execution (15 pieces total)
	</li>
	<li>
		Select top quality pieces for presentation that successfully demonstrate mastery of drawing
		issues and are no larger than 18” x 24” (5 pieces total)
	</li>
</ul>

