<h2 class="text-3xl mb-4 ml-24 mt-24">Features</h2>

<ul class="list-disc list-inside ml-24 space-y-2 text-xl">
	<li><strong>Course Selection</strong>: Browse and select from 150+ courses with detailed info!</li>
	<li><strong>4-Year Planning</strong>: Plan courses across freshman through senior year, fall and spring semesters.</li>
	<li><strong>4x4 Schedule</strong>: Built specifically for students on the 4x4 schedule</li>
	<li><strong>Course Profiles</strong>: View detailed information including prerequisites, difficulty, homework load, and course descriptions.</li>
	<li><strong>Graduation Requirements</strong>: Track progress toward graduation requirements</li>
	<li><strong>Easy Searching</strong>: You are able to search for classes, and filter them by type, homework time, difficulty, and more!</li>
	<li><strong>User Authentication</strong>: Login with Google and GitHub (through Google Firebase)</li>
	<li><strong>Data Persistence</strong>: Your course selections are saved to the cloud</li>
</ul>


